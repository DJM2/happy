                    <p class="p-tours">
                        Paucartambo is a district of Cusco located at 2900 meters above sea level, with a variety of
                        flora, fauna and ancestral customs.<br><br>
                        In the viewpoint of three crosses with its cloudy forest at 3850m.a.s.l. it is possible to
                        observe a natural spectacle, contemplating colors and tonalities in the sky, the light and how
                        the sun rises in a very different way to what we may be used to. , this phenomenon is observed
                        only in 2 places in the world: Paucartambo and Japan, therefore this experience will always be
                        in your mind as something surreal.<br><br>

                        At the same time from this viewpoint you can see the chain of mountains of the snowy Ausangate
                        that contrasts with the Paucartambo river.
                    </p>
                    <img src="{{ asset('img/blog/thumb/Mirador-de-3-cruces-Paucartambo.webp') }}"
