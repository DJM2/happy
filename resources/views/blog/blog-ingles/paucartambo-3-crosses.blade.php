                    <h2> How to get?</h2>
                    <p>
                        From the city of <strong>Cusco,</strong> we take our mobility heading southeast along the paved
                        road and travel
                        108 km for an average of 2 hours until we reach <strong>PAUCARTAMBO,</strong> where we will
                        observe the Carlos
                        III bridge, the temple, and within this, the <strong>Virgen del Carmen</strong>, who is highly
                        venerated with
                        music and dances in her festivity that begins on July 15 until July 18.<br><br>

                        From Paucartambo, we will continue our journey of 45 km for two more hours, heading to the
                        viewpoint of three golden crosses that is located within the Manu National Park, which is one of
                        the reserves that houses an exquisite biodiversity recognized worldwide, currently protected of
                        human impact and was nominated as a world natural heritage in 1987.
                    </p>

                    <h3> <strong>NOTE:</strong></h3>
                    <p>
                        Before going to the viewpoint, you must pay an entrance ticket to the Manu National Park of
                        10.00 nuevos soles for adults, and 6.00 soles for children.<br><br>

                        After your experience at the viewpoint, you can visit, for example, the historical cultural zone
                        "Pusharo" with an extra entrance fee of 50.00 soles, as well as the Manu River area with an
                        entrance fee of 150.00 soles and you will have to be accompanied by an authorized park ranger.
                    </p>

                    <h2> What should you bring to Mirador 3 cóndores?</h2>
                    <ul>
                        <li>Warm, light and waterproof clothing.</li>
                        <li> Backpack.</li>
                        <li> Sunscreen and repellent.</li>
                        <li> Cap or hat.</li>
                        <li> Water and snacks.</li>
                        <li> Cash.</li>
                        <li> Camera.</li>
                    </ul>

                    <h2> What is the best time to go?</h2>

                    <p>
                        It is recommended to visit the viewpoint between the months of May to July; highlighting the 20,
                        21 and 22 of June since in these days the winter solstice occurs and hopefully it does not dawn
                        with clouds that they prevent you from having impressive views; or also another unmissable date
                        is on the feast of the Virgen del Carmen that begins on July 15 and ends on July 18 where
                        thousands of devotees meet and present artistic, religious, cultural, and gastronomic activities
                        Showing thus its customs and traditions to all locals and visitors, not for nothing Paucartambo
                        is recognized as "folk capital of Cusco".
                    </p>

                    <h2> Do you dare to go see the sunrise?</h2>
                    <p>
                        This experience is highly recommended for those who like nature and want to get out of the
                        stress of the city either in the company of friends and/or family. <br><br>
                        The time to contemplate this natural phenomenon is from 4:30 to 5:30 in the morning.
                        The sun's rays illuminate as paths in the humid sky for several minutes, distorting the
                        image by giving an optical illusion of three suns, as if each sun jumped from one side to
                        other.<br><br>
                        Some agencies offer a two-day tour to Paucartambo. Starting very early in the city
                        from Cusco in the direction of Paucartambo, and during the trip make some stops to visit
                        the archaeological complex of "Ninamarca" and its pre-Inca chullpas. <br> Upon reaching the town
                        visit
                        the temple of the Virgen del Carmen or the museum of typical dances. The trek to the viewpoint
                        of the three crossings is carried out at night and camping in authorized sites and waiting for a
                        Spectacular sunrise that illuminates the dense jungle of Manu.
                    </p>

