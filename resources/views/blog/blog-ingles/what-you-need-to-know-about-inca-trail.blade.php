                    <h5 class="h5-tours"> DESCRIPTION OF THE INCA TRAIL</h5>
                    
                    <ul class="text-justify">
                        <li> The road consists of 42 kilometers, starting from the town of Piscacucho, also known as
                            like the km 82 that counts the railway line that goes from Cusco to the Hydroelectric, this
                            adventure
                            begins the <strong>first day </strong> through the towns of Mescay, Tarayoc, Hatunchaca and
                            finally
                            Huayllabamaba, the <strong>second day </strong> through the camps of Yuncachimpa, Ayapata,
                            Llulluchapamapa, the pass of warmihuañusca (4215m.a.s.l.) and Pacaymayo. <br> The
                            <strong>third day </strong>
                            archaeological sites of Runkuracay and Sayacmarca, the chaquicocha camp and
                            Phuyupatamarca to finally finish by the agricultural terraces of intipata and rest
                            in the last Wiñayhuayna camp. <br> The <strong> fourth and last day </strong> to the door of
                            the sun
                            or intipuku and finish the tour in the historic sanctuary of Machu Picchu.
                        </li> <br>

