                    <h5 class="h5-tours"> The purpose of the Inca Trail regulations:</h5>
                    <ul class="text-justify">
                        <li> 1.- Protect and conserve the cultural and natural heritage of the Inca trail network of the
                            Historic Sanctuary of Machu Picchu.</li> 
                        <li> 2.- Promote the sustainable use of the cultural and natural landscape of the network of
                            Inca trails of the Historic Sanctuary of Machu Picchu, in accordance with current
                            legislation on conservation and development of tourist activity.</li> 
                        <li> 3.- Prevent, mitigate, reduce and/or control possible impacts on the cultural and natural
                            heritage resulting from the use of the Inca Trail Network of the Historic Sanctuary of Machu
                            Picchu.</li> 
                        <li> 4.- Promote the commitment of users to the conservation of the cultural and natural
                            heritage of the Inca Trail Network of the Historic Sanctuary of Machu Picchu.</li> 
                        <li> 5.- Disseminate codes of conduct and ethical standards for responsible tourism activity to
                            contribute to the conservation of users of the Inca Trail Network of the Historic Sanctuary
                            of Machu Picchu.</li> 
                        <li> 6.- Promote the development, implementation and proper use of the infrastructure services
                            of the Inca Trail Network of the Historic Sanctuary of Machu Picchu.</li> 
                        <li> 7.- Commit the participation of the competent entities and the actors involved to improve
                            the management and provide an adequate provision of tourist services of the Inca Trail
                            Network of the Historic Sanctuary of Machu Picchu.</li>
                        <li> 8.- Promote prevention actions as part of the risk management contingency plans of the
                            Historic Sanctuary of Machu Picchu.</li> 
                        <li> 9.- Promote and coordinate the training of professional and technical personnel of the
                            competent entities, support personnel, local population and involved actors that provide
                            services on the Inca Trail Network.</li>
                    </ul>
