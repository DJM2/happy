                    <div class="separador-naranja"></div>
                    <div class="separador-azul"></div>
                    <p class="p-tours">
                        In 2021, Peru participated in the World Travel Awards (WTA), achieving the designation of best
                        cultural and culinary destination in the world. <br>
                        Similarly, on November 23, 2021, the Peruvian restaurant Central, owned by renowned chefs
                        Virgilio Martínez and Pía León, was recognized for its gastronomic exploration of Peru's
                        biodiversity and took first place as the best restaurant in Latin America. <br> <br>

                        <img src="{{ asset('img/blog/gastronomia-peruana.webp') }}" alt="Peruvian gastronomy"
                            loading="lazzy"width="100%"> <br> <br>

                        There is no doubt that Peru is in the eyes of the world, we have the great fortune of having 3
                        different well marked regions such as La Costa, La Sierra and La Selva and each region is very
                        particular for their microclimates that exist within them, with the consequent production
                        agricultural variety of products that makes Peruvian gastronomy very diverse.<br>
                        Next, we leave a list of dishes that you cannot miss when you visit Peru:

                    </p><br>
                    <h5 class="h5-tours">&#9679; <u> CEBICHE </u> </h5>
                    <p class="p-tours">
                        It is not very clear where the name of this Peruvian flag dish comes from, perhaps the closest
                        thing to the name is "SIBECH" which in the Arabic language means "acidic or citric food".
                        Some investigations direct that this dish was consumed 2000 years ago in Peru, being the Tallan
                        civilization that developed in what we now know as the Piura area, the first people to consume
                        it, in the same way these investigations refer to the Mochicas as diners of this delicacy, being
                        the dish prepared with fresh fish and marinated with the fermented juice of tumbo (a citrus
                        fruit from northern Peru, family of the passion fruit). <br> During the Inca times this dish was
                        also
                        consumed, but with the difference that in the Peruvian highlands it was marinated with chicha,
                        the sacred drink of the Incas that comes from the fermentation of corn.
                        With the conquest of the Spanish, the consumption of this dish in Inca lands begins to be left
                        in written evidence thanks to its chronicles, where it is mentioned that this delicacy contains
                        salt and chili in its preparation. <br> With the presence of the Spaniards, the dish was
                        improved,
                        for this reason they are the ones who add the onion and sour oranges for its preparation, which
                        was similar to the already mentioned Mediterranean food Sibech.<br>
                        In the middle of the 20th century in the city of Lima, Peruvians added more ingredients to make
                        this delicacy our representative dish, adding sweet potato, corn, lettuce, parsley and yuyo or
                        seaweed, in the same way the sour orange was changed by the Peruvian lemon so that the cooking
                        of the fish is faster.
                    </p>
                    <img src="{{ asset('img/blog/cebiche-peruano.webp') }}" alt="Peruvian ceviche" loading="lazzy"
                        width="100%"><span>Picture: Ceviche, peruvian typical dish</span> <br><br>

                    <h5 class="h5-tours"> &#9679;<u> AJÍ DE GALLINA </u></h5>
                    <p class="p-tours">
                        The history of this well-known dish takes us to Spain, where during the 14th century it was very
                        common in society to consume manjar blanco, a sandwich containing boiled chicken breast,
                        seasoned with sugar, walnuts and almonds, thickened with flour and rice, which is currently
                        consumed in Spanish gastronomy, over time this dessert underwent some changes to become a much
                        colder dessert, leaving aside the chicken and adding nuts and sugar, remaining slightly sweet
                        for the palate of diners. <br> In colonial times this dish entered Peruvian lands and was well
                        received by diners, the strange thing was that it was no longer a dessert that was served after
                        the main course, but instead became the main dish due to its unique flavor, without a doubt. It
                        was the most requested dish by Peruvians and Spaniards.<br> As time passed, the Peruvians began
                        to improve this delicacy even more, the yellow pepper was incorporated into the preparation,
                        which gives it a yellowish color and an indisputable flavor, in this way the dessert ceased to
                        be sweet and became a plate of highly sought-after fund in the country.<br>
                        According to Rodolfo Tafur, gastronomic researcher, in 1839, this dish became so popular that
                        there was no Peruvian restaurant that did not offer it, additionally, in those times the dish
                        was prepared with pieces of boiled chicken meat, onion previously fried in the pan, crushed
                        garlic, small pieces of bread, and obviously the yellow pepper to give it color, all accompanied
                        by white rice.
                    </p>
                    <img src="{{ asset('img/blog/aji-de-gallina.webp') }}" alt="Aji de gallina, Perú" loading="lazzy"
                        width="100%"><span>Picture: Aji de gallina, Perú</span> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> CAUSA RELLENA </u></h5>
                    <p class="p-tours">
                        There is no doubt that Peru is the land of the potato, according to statistical data in this
                        country more than 3500 varieties of potato are grown, being the main food in Peruvian cuisine,
                        this tuber was planted for the first time in the southern part of the Peru Approximately 5000
                        BC, today there are still high Andean communities that preserve the ancient techniques of
                        planting and harvesting said tuber, a clear example of such is the potato park in the department
                        of Cusco, province of Urubamba, district of Pisaq , The Potato Park is a Biocultural Territory
                        dedicated to the conservation of biological and cultural diversity in the Andes mountains. It is
                        managed by the five indigenous communities of Amaru, Chawaytire, Pampallacta, Paru Paru and
                        Sacaca, with the support of the Association for Nature and Sustainable Development, ANDES.
                        The name of the stuffed cause comes from the quecha word "Kausay" which in Spanish means "life"
                        in Inca times basically it was boiled potatoes with slices of chili and was usually eaten when
                        working in the cultivation places. Already in colonial times, the Spanish added more ingredients
                        such as olives, avocado, eggs, fish, etc.
                        Another theory comes from the Pacific War, where Chile was fighting against Peru and Bolivia, it
                        is said that when the Peruvian army invaded Chile, there was no longer enough food for the
                        soldiers, it is for this reason that the women who accompanied them gathered what little they
                        they had leftovers that were basically potato, lemon, chili, salt and pepper, they mixed
                        everything and this dish came out, at the time of feeding the soldiers they constantly repeated
                        this phrase “for the cause”. <br> <br>

                        <img src="{{ asset('img/blog/causa-limeña.webp') }}" alt="Causa limeña" loading="lazzy"
                            width="100%"><span>Picture: Causa limeña</span> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> LOMO SALTADO </u></h5>
                    <p class="p-tours">
                        This dish, unlike others, has no origin in the Inca era, so it is not native to Peru. The origin
                        of this dish comes from the colonial era exactly in 1538, when the first beef arrived in Peru,
                        it is then that the Spanish brought fried meat as an appetizer, it was one of the most expensive
                        dishes at that time since the meat came from Spain to Peru, as time passed the price of meat was
                        falling and it was already more accessible to the population, then in 1849 with the arrival of
                        the Chinese in Peru this dish was improving, the Asians began to fuse its condiments and
                        culinary arts with the ingredients that were available at the time and as a result you have the
                        famous lomo saltado, which is if it is the fusion of 3 cultures in a single dish, since you have
                        beef, onion and tomato brought from Spain, the way of sauteing vegetables with soy sauce that is
                        the hallmark of Chinese cuisine, in the same way we have rice brought by the Chinese and as a
                        final complement the potatoes that are native to Peru. <br>
                        Nowadays there are many options to enjoy this dish, in some restaurants chicken meat is used
                        instead of beef, in the highlands of Peru it can be found with alpaca or lamb meat, and in the
                        same way for some people who they are vegetarian you can find them with soy meat, but yes, the
                        type of meat could vary but the way of preparing it will always be the same, you cannot lose the
                        essence of the food. <br> <br>

                        <img src="{{ asset('img/blog/lomo-saltado.webp') }}" alt="Lomo saltado plato tipico de Perú" loading="lazzy"
                            width="100%"><span>Picture: Lomo saltado</span> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> CUY </u></h5>
                    <p class="p-tours">
                        In the world there are only 3 places where you can eat this delicious dish, these countries are
                        Bolivia, Ecuador and obviously in Peru, according to history this dish was consumed since
                        pre-Inca times, which means that people consumed it between 250 300 years before Christ, at the
                        time of the Incas this dish was a delicacy that was consumed by people of nobility and normally
                        on important dates, in the same way it is known from history that the Incas sacrificed these
                        rodents to be able to make offerings to their gods, an example of this is in Machu Picchu, where
                        in the temple of the condor in the excavations that were made, bones of guinea pigs, alpacas and
                        llamas were found. There is no doubt that this was an important animal within the Andean
                        religion. <br>
                        It is very uncertain how these rodents arrived in Europe, Asia or North America, and why they
                        are not consumed as meat in these continents, since they are usually pets for families. The
                        story goes that when Christopher Columbus discovered America, on his return trip to Europe he
                        brought different types of gifts from American lands, such as examples of earth, stones, plants,
                        etc. In the same way, he brought the guinea pigs that were given as gifts for the families that
                        financially supported their expedition, and it is there that at that time only some wealthy
                        families in Europe had such an exotic animal that it was impossible to find, hence part of the
                        reason why they began to be raised as pets since if they were they ate it, so they couldn't show
                        off such a rare animal for the time and place. Other legends say that this animal was brought to
                        Europe by the Spanish after the conquest of the Incas in 1535, to be given as gifts to the King
                        of Spain and all the nobility. <br>
                        It is known that guinea pig meat has a high nutritional value, as it is a source of animal
                        protein and, at the same time, a food low in fat and cholesterol. As seen in the table, its
                        protein value is 20.3%, surpassing chicken and beef, which only have 18.3% and 17.5%
                        respectively, and also pork, which only reaches 14.5% protein. . Similarly, while guinea pig has
                        only 7.8% fat, beef reaches 21.8% and pork 37.3%, which confirms its advantages over other
                        meats. <br>
                        <strong>There are different ways to consume it, it can be fried, baked, in soup, grilled
                            etc. </strong> <br> <br>

