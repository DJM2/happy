                    <h2 class="h2-happy">
                        <o>La Exquisita</o>
                        <m> Gastronomía Peruana </m>
                    </h2>
                    <div class="separador-naranja"></div>
                    <div class="separador-azul"></div>
                    <p class="p-tours">
                        En el año 2021 el Perú participo de los premios World Travel Awards (WTA), consiguiendo la
                        denominación de mejor destino cultural y culinario en todo el mundo. <br>
                        Del mismo modo 23 de noviembre del 2021 el restaurante peruano Central, de los reconocidos chefs
                        Virgilio Martínez y Pía León, fue reconocido por su exploración gastronómica de la biodiversidad
                        del Perú y se llevó el primer puesto como mejor restaurante de América Latina. <br> <br>

                        <img src="{{ asset('img/blog/gastronomia-peruana.webp') }}" alt="Gastronomía Peruana"
                            loading="lazzy" width="100%"> <br> <br>

                        No cabe duda que el Perú esta en los ojos del mundo, tenemos la gran fortuna de tener 3
                        diferentes regiones bien marcadas como son La Costa, La sierra y la Selva y cada región es muy
                        particular por sus micro climas que existen dentro de ellas, con la consecuente producción
                        agrícola de productos variados que hace que la gastronomía peruana sea muy diversa. <br>
                        A continuación, dejamos una lista de platos que no puedes dejar de probarlos cuando vistes Perú.
                    </p><br>

                    <h5 class="h5-tours">&#9679; <u> CEBICHE </u> </h5>
                    <p class="p-tours">
                        No se acierta muy bien de donde viene el nombre de este plato bandera peruano, quizá lo más
                        próximo al nombre sea “SIBECH” que en el idioma árabe significa “comida acida o cítrica”.
                        Algunas investigaciones dirigen que este plato se consumía 2000 años atrás en el Perú, siendo la
                        civilización Tallan que se desarrolló en lo que ahora conocemos como la zona de Piura las
                        primeras personas en consumirlos, del mismo modo estas investigaciones refieren a los mochicas
                        como comensales de esta delicia, siendo el plato preparado con pescado fresco y marinado con el
                        jugo fermentado de tumbo, (una fruta cítrica del norte peruano, familia del maracuyá). <br>
                        Durante la época incaica este plato también era consumido, pero con la diferencia que en la
                        sierra peruana lo marinaban con la chicha, bebida sagrada de los incas que proviene de la
                        fermentación del maíz. Con la conquista de los españoles se empieza a dejar en evidencia escrita
                        gracias a sus crónicas el consumo de dicho plato en tierras incaicas, donde se menciona que esta
                        delicia contiene sal y ají dentro de su preparación. <br> Con la presencia de los españoles el
                        plato se fue mejorando, por tal razón son ellos los que agregan la cebolla y las naranjas agrias
                        para su preparación, lo cual era parecido a la ya mencionada comida mediterránea Sibech. <br>
                        A mediados del siglo XX en la ciudad de Lima los peruanos agregamos mas ingredientes para hacer
                        de esta delicia nuestro plato representativo, se agregó el camote, choclo, lechuga, perejil y el
                        yuyo o el alga marina, del mismo modo se cambio la naranja agria por el limón peruano para que
                        la cocción del pescado sea más rápida.
                    </p>
                    <img src="{{ asset('img/blog/cebiche-peruano.webp') }}" alt="Ceviche peruano" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours"> &#9679;<u> AJÍ DE GALLINA </u></h5>
                    <p class="p-tours">
                        La historia de este conocidísimo plato nos lleva a España, que durante el siglo XIV dentro de la
                        sociedad era muy común el consumir el manjar blanco, un bocadillo que contenía pechuga de
                        gallina hervida, sazonada con azúcar, nueces y almendras, espesada con harina y arroz, que
                        actualmente se consume en la gastronomía española, con el pasar del tiempo este postre sufrió
                        algunos cambios para convertirse en un postre mucho más frio, dejando de la lado la gallina y
                        agregándole frutos secos y azúcar, quedando ligeramente dulce para el paladar de los comensales.
                        <br> En la época colonial este plato entra a tierras peruanas y teniendo buena acogida por los
                        comensales, lo raro fue que ya no era un postre que se servía después del plato principal, sino
                        cambio a ser el plato principal por su singular sabor, sin duda alguna fue el plato más
                        solicitado por los peruanos y españoles. <br> Al pasar el tiempo los peruanos comenzaron a
                        mejorar aun mas esta delicia, se incorporó en la preparación el ají amarillo, que le da el color
                        amarillento y un sabor indiscutible, de este modo el postre dejo de ser dulce y paso a ser un
                        plato de fondo muy solicitado en el país. <br>
                        Según Rodolfo Tafur, investigador gastronómico, en 1839, este plato se popularizó tanto, que no
                        había restaurante peruano que no lo ofreciera, adicionalmente, en esos tiempos el plato se
                        preparaba con trozos de carne de gallina hervida, cebolla sofrita previamente en la sartén, ajos
                        machacados, trozos pequeños de pan, y evidentemente el ají amarillo para darle color, todo ello
                        acompañado de arroz blanco.
                    </p>
                    <img src="{{ asset('img/blog/aji-de-gallina.webp') }}" alt="Plato aji de gallina Perú" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> CAUSA RELLENA </u></h5>
                    <p class="p-tours">
                        No cabe duda que le Perú es la tierra de la papa, según datos estadísticos en este país se
                        cultiva mas de 3500 variedades de papa, siendo el alimento principal dentro de la gastronomía
                        peruana, este tubérculo fue sembrado por primera vez en la parte sur del Perú 5000 a.c
                        aproximadamente, hoy en día aun existen comunidades alto andinas que preservan la técnicas
                        milenarias de sembrío y cosecha de dicho tubérculo, un claro ejemplo de tal es el parque de la
                        papa en el departamento de Cusco, provincia de Urubamba, distrito de Pisaq. <br>El Parque de la
                        Papa
                        es un Territorio Biocultural dedicada a la conservación de la diversidad biológica y cultural en
                        las montañas de los Andes. Es administrado por las cinco comunidades indígenas de Amaru,
                        Chawaytire, Pampallacta, Paru Paru y Sacaca, con el apoyo de la Asociación para la Naturaleza y
                        el Desarrollo Sostenible, ANDES. <br>
                        El nombre de la causa rellena proviene de la palabra quecha “Kausay” que en el español significa
                        “vida” en tiempos incaicos básicamente era la papa hervida con rodajas de ají y se comía
                        habitualmente cuando se trabajaba en los lugares de cultivo. Ya en época colonial los españoles
                        agregaron mas ingredientes como la aceituna, la palta, huevos, pescado etc. <br>
                        Otra teoría viene de la guerra del pacifico, donde Chile luchaba contra Perú y Bolivia, se dice
                        que cuando el ejercito peruano invadía Chile, ya no había suficiente comida para los soldados,
                        es por tal razón que las mujeres que acompañaban, reunieron lo poco que les sobraba que
                        básicamente era papa, limón, ají, sal y pimienta lo mezclaron todo y salió este plato, al
                        momento de alimentar a los soldados repetían constantemente esta frase “por la causa”.
                        <br> <br>

                        <img src="{{ asset('img/blog/causa-limeña.webp') }}" alt="Gastronomia causa limeña Perú" loading="lazzy"
                            width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> LOMO SALTADO </u></h5>
                    <p class="p-tours">
                        Este plato en diferencia con otros tiene ningún origen en la época incaica por tal modo no es
                        oriundo del Perú. El origen de este plato viene de la época colonial exactamente en 1538, cuando
                        las primeras carnes de res llegaron la Perú, es en ese entonces que los españoles trajeron la
                        carne frita como aperitivo, era uno de los platos mas caros en ese entonces ya que la carne
                        venia desde España hasta el Perú, al pasar el tiempo el precio de la carne iba bajando y ya era
                        mas accesible para la población, luego en el año 1849 con la llegada de los Chinos al Perú este
                        plato iba mejorando, los asiáticos comenzaron a fusionar sus condimentos y artes culinarios con
                        los ingredientes que se tenía en la época y como resultado se tiene el famoso lomo saltado, que
                        es si es la fusión de 3 culturas en un solo plato, ya que se tiene la carne de res cebolla y
                        tomate traída de España, la forma de saltear las verduras con sillao que es el sello de la
                        cocina china, del mismo modo tenemos arroz traído por los Chinos y como complemento final las
                        papas que es oriundo del Perú. <br>
                        Ahora en día se tiene bastantes opciones para poder disfrutar este plato, en algunos
                        restaurantes se pone carne de pollo en ves de res, en la sierra del Perú se puede encontrar con
                        carne de alpaca o cordero, y de la misma forma para algunas personas que son vegetarianas se
                        puede encontrar con la carne de soya, pero eso si, el tipo de carne podría variar pero la forma
                        de preparar siempre será la misma, no se puede perder la esencia de la comida. <br> <br>

                        <img src="{{ asset('img/blog/lomo-saltado.webp') }}" alt="Palto tipico - lomo saltado Perú" loading="lazzy"
                            width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> CUY </u></h5>
                    <p class="p-tours">
                        En el mundo solo existe 3 lugares donde se podrá comer este delicioso plato, estos países son
                        Bolivia, Ecuador y obviamente en el Perú, según la historia este plato fue consumido desde la
                        épocas pre incas, lo que supone que las personas lo consumían entre 250 a 300 años antes de
                        Cristo, en la época de los incas este plato era una delicia que se consumía por gente de nobleza
                        y en fechas importantes normalmente, del mismo modo se sabe por historia que los incas
                        sacrificaban estos roedores para poder hacer ofrendas a sus dioses, ejemplo de ello se tiene en
                        Machu Picchu, que en el templo del condor en las excavaciones que se hizo se encontraron huesos
                        de cuys, alpacas y llamas. No cabe ninguna duda que este era un importante animal dentro de la
                        religión andina. <br>
                        Es muy incierto de como llegaron estos roedores a Europa, Asia o Norte América, y el por que no
                        se consume como carne en dichos continentes, ya que estos habitualmente son mascotas para las
                        familias. Cuenta la historia que cuando Cristobal Colon descubre America, en su viaje de vuelta
                        a Europa el lleva diferentes tipos de regalos de tierras americanas, como ejemplos de tierra,
                        piedras, plantas etc, del mismo modo lleva los cuys que fueron entregados como regalos para las
                        familias que apoyaron económicamente en su expedición, y es ahí que en ese tiempo solo algunas
                        familias adineradas en Europa tenían un animal tan exotico que era imposible de encontrar, de
                        ahí parte la razón del por que se comienza a criar como mascotas ya que si se lo comían, luego
                        no podrían presumir a tan raro animal para la época y el lugar. Otras leyendas cuentan que ese
                        animal fue llevado a Europa por los españoles después de la conquista a los incas en el año
                        1535, para ser entregado como regalos para el rey de España y toda la nobleza. <br>
                        Se sabe que la carne de cuy tiene un alto valor nutritivo, pues es una fuente de proteína animal
                        y, a la vez, un alimento bajo en grasas y colesterol. Como se ve en el cu adro, su valor
                        proteínico es de 20.3 %, superando a la carne de pollo y de vacuno, que sólo tienen 18.3 % y
                        17.5 % respectivamente, y también a la de cerdo que llega sólo a un 14.5 % de proteína. De igual
                        manera, mientras el cuy tiene sólo el 7.8 % de grasa, la carne de vacuno alcanza el 21.8 % y la
                        del cerdo el 37.3 %, lo que confirma sus ventajas sobre otras carnes. <br>
                        <strong>Existen diferentes formas de consumirlo, puede ser frito, al horno, en sopa, a la braza
                            etc. </strong> <br> <br>

