                    <img src="{{ asset('img/blog/inca-trail-camino.webp') }}" alt="Inca trail to Machu Picchu"
                        class="img-fluid" class="text-center" loading="lazy"><br><br>
                    <p class="p-tours">
                        Sin ninguna duda el camino inca es uno de los Trek más conocidos y cotizados en el mundo del
                        senderismo, es por tal razón que mediante Decreto Supremo N° 001-81-AA de fecha 8 de enero de
                        1981 se estableció el Santuario Histórico de Machu Picchu como Área Natural Protegida por el
                        Estado, lo cual implica un arduo control dentro del camino para su conservación. En comparación
                        con otras caminatas en el Perú, el camino inca es el único sendero en el cual se tiene que
                        reservar su ingreso con anticipación y también hacer un depósito de dinero a cuentas del estado,
                        dicho dinero será utilizado para poder hacer el mantenimiento de la ruta, pago al personal
                        administrativo, guardaparques etc.
                        En el año 2016 en el informe N°727-2016-SERNANP-DGANP y 366-2016-OAJ de fecha 5 y 17 de
                        diciembre de 2016, mediante los cuales la Dirección de Gestión de las Áreas Naturales Protegidas
                        y la Asesoría Jurídica del Servicio Nacional de Áreas Naturales Protegidas por el estado,
                        respectivamente, recomiendan la aprobación del Reglamento de uso turístico sostenible de la Red
                        de Caminos Inca del <strong>Santuario Histórico de Machu Picchu. </strong>
                    </p><br>

                    <img src="{{ asset('img/blog/inca-trail-santuario.webp') }}" alt="Inca trail santuario machu Picchu"
                        class="img-fluid" loading="lazy"><br><br>

                    <h5 class="h5-tours"> La finalidad del reglamento del Camino Inca :</h5>
                    <p class="p-tours text-center">
                    <ul>
                        <li> 1.- Proteger y conservar el patrimonio cultural y natural de la red de caminos inca del
                            Santuario Histórico de Machu Picchu.</li>
                        <li> 2.- Promover el uso sostenible del paisaje cultural y natural de la red de caminos inca del
                            Santuario Histórico de Machu Picchu, de acuerdo a la legislación vigente sobre conservación
                            y desarrollo de la actividad turística.</li>
                        <li> 3.- Prevenir, mitigar, reducir y/o controlar los posibles impactos al patrimonio cultural y
                            natural producto del uso de la Red de caminos inca del Santuario Histórico de Machu Picchu.
                        </li>
                        <li> 4.- Propiciar el compromiso de los usuarios de la conservación del patrimonio cultural y
                            natural de la Red de caminos inca del Santuario Histórico de Machu Picchu.</li>
                        <li> 5.- Difundir códigos de conducta y normas de ética de la actividad turística responsable
                            para contribuir a la conservación de parte de los usuarios de la Red de caminos inca del
                            Santuario Histórico de Machu Picchu.</li>
                        <li> 6.- Promover el desarrollo, implementación y uso adecuado de los servicios de
                            infraestructura de la Red de caminos inca del Santuario Histórico de Machu Picchu. </li>

                        <li> 7.- Comprometer la participación de las entidades competentes y de los actores involucrados
                            para mejorar la gestión y brindar una adecuada prestación de servicios turísticos de la Red
                            de caminos inca del Santuario Histórico de Machu Picchu.</li>
                        <li> 8.- Impulsar acciones de prevención como parte de los planes de contingencia de la gestión
                            de riesgo del Santuario Histórico de Machu Picchu.</li>
                        <li> 9.- Promover y coordinar la capacitación del personal profesional y técnico de las
                            entidades competentes, personal de apoyo, población local y actores involucrados que brindan
                            servicios en la Red de Caminos Inca.</li>
                    </ul>
                    </p> <br>

                    <img src="{{ asset('img/blog/inca-trail-machu-picchu.webp') }}" alt="Inca trail to Machu Picchu"
                        class="img-fluid" class="text-center" loading="lazy"><br><br>

                    <p class="p-tours">
                        Dentro del camino encontraremos también trabajando a los porteadores que son personas que se
                        dedican a transportar equipajes, enseres, alimentos, entre otros artículos de uso personal que
                        llevan los turistas y expedicionarios del Camino Inca.<br> <br>

                        <img src="{{ asset('img/blog/inca-trail-reglamento-porteadores.webp') }}"
                            alt="Inca trail to Machu Picchu" class="img-fluid" class="text-center"
                            loading="lazy"><br> <br>

                        Antes de la creación del reglamento
                        de protección laboral de los porteadores, estos eran explotados por las agencias de viajes,
                        obligando a dichas personas a cargar entre 40 a 50 kilos por día, del mismo modo no se les
                        incluía víveres para su alimentación en el camino, por tal motivo muchos porteadores sufrían a
                        futuro problemas de salud. <br>
                        Ahora en día los porteadores también tienen su reglamento dentro del camino inca siendo los más
                        importantes los siguientes:

                    <ul>
                        <li> El peso máximo de carga por porteador es de 20 kilos y la remuneración mínima por 4 días de
                            trabajo es equivalente a 1.2% de una UIT</li> <br>
                        <li> Las agencias operadoras deben de proveer de ropa y calzado para los porteadores y un seguro
                            de vida. </li> <br>
                        <li> La institución encargada de hacer cumplir todo esto es el SUNAFIL (Superintendencia
                            Nacional de Fiscalización Laboral).</li> <br>
                    </ul>
                    </p>

