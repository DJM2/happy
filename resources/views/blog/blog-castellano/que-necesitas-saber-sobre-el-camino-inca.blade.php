                    <h5 class="h5-tours">Descripción del Camino Inca</h5>
                    <p class="p-tours text-center">
                    <ul>
                        <li> El camino consta de 42 kilómetros, empezando del poblado de Piscacucho también conocido
                            como el km 82 que cuenta la línea férrea que va de Cusco a la Hidroeléctrica, esta aventura
                            empieza el <strong>primer día </strong> pasando por los poblados de Mescay, Tarayoc,
                            Hatunchaca y finalmente Huayllabamaba, el <strong>segundo día </strong> se pasa por los
                            campamentos de Yuncachimpa, Ayapata, Llulluchapamapa, el paso de warmihuañusca
                            (4215m.s.n.m.) y Pacaymayo. <br> El
                            <strong>tercer día </strong>
                            por los lugares arqueológicos de Runkuracay y Sayacmarca, el campamento de chaquicocha y
                            Phuyupatamarca para finalmente terminar por las terrazas agrícolas de intipata y descansar
                            en el último campamento de Wiñayhuayna. <br> El <strong> cuarto y último día </strong> se va
                            hacia la puerta del sol o intipuku y terminar el recorrido en el santuario histórico de
                            Machu Picchu.
                        </li> <br>

                        <img src="{{ asset('img/blog/inca-trail-campamento.webp') }}" alt="Inca trail to Machu Picchu"
                            class="img-fluid" class="text-center" loading="lazy"><br>


                        <img src="{{ asset('img/blog/inca-trail-paisaje3.webp') }}" alt="Inca trail to Machu Picchu"
                            class="img-fluid" class="text-center" loading="lazy"><br> <br>


                        <li> La dificultad del camino es <strong>moderada – difícil </strong>, el punto más alto es de
                            4215 m.s.n.m. y el punto mas bajo es el campamento de Wiñayhuayna 2600 m.s.n.m. la
                            temperatura es variada cada día, el <strong>primer día</strong> la sensación térmica durante
                            el día es entre 20° a 25° C. y por la noche entre 10° a 12° C, el <strong>segundo
                                día</strong> por el día entre 10° a 20° C durante el día y por la noche entre 5° a 8°C,
                            <strong> el tercer día </strong> es mas tropical y húmedo, la temperatura oscila entre 22° a
                            28°C.
                        </li> <br>

                        <img src="{{ asset('img/blog/inca-trail-paisaje2.webp') }}" alt="Inca trail to Machu Picchu"
                            class="img-fluid" class="text-center" loading="lazy"><br>

                        <img src="{{ asset('img/blog/inca-trail-campamento2.webp') }}"
                            alt="Inca trail to Machu Picchu" class="img-fluid" class="text-center"
                            loading="lazy"><br> <br>

                        <li> Durante el año se tiene dos épocas bien marcadas; de Abril a octubre la temporada de secas
                            y de noviembre a marzo la temporada de lluvias. De todos modos, como en todas las caminatas
                            el clima es muy cambiante, no se sorprendan si cae lluvia en la temporada de secas, por eso
                            es recomendable estar listos para tener cuatro temporadas en un solo día.

                        </li> <br>

