                    <div class="separador-azul"></div>
                    <p class="p-tours">
                        <strong><i> “A unos 100 kilómetros al sureste de Cusco, Perú, existe un arcoíris hecho
                                montaña.” </i></strong> <br> <br>
                        Cusco, tiene consigo lugares turísticos extraordinarios, como es el caso de Vinicunca o
                        Winikunka, Montaña de Siete Colores, Montaña Arcoíris, Montaña de Colores, o como prefieras
                        llamar a este privilegio de la naturaleza. <a class="enlaces" href="../../es/vinicunca-tour">Ver tour a Vinicunca</a> </p>

