                    <h2><u>Ubicación de la montaña de colores</u></h2>
                    <p class="p-tours">
                        La montaña se encuentra en la Cordillera del Vilcanota a 5.200 metros sobre el nivel del mar,
                        entre los distritos de Cusipata, provincia de Quispicanchi y Pitumarca, provincia de
                        Canchis.<br>

                        Sus laderas y cumbres están teñidas por unas franjas de intensos tonos de fucsia, turquesa,
                        lavanda y dorado.
                        El espectáculo visual que ofrece Vinicunca atrae visitantes desde inicios de 2016, según
                        informes de la Municipalidad de Pitumarca. <br>

                        En poco más de dos años, los viajeros que llegan a tomarse fotos con la cumbre pasaron de unas
                        pocas decenas a cerca de 1.000 al día, aseguran medios peruanos, pese al frío y la altitud del
                        lugar.<br>
                        Gracias a su gran popularidad, impulsada en gran parte también por las diversas redes sociales,
                        la montaña se convirtió en un destino habitual en los rankings de atractivos turísticos
                        mundiales.<br>
                        Por ejemplo, en agosto de 2017, apareció en la lista de 100 lugares para visitar antes de morir
                        recomendados por expertos en viajes en la página web “Business Insider”
                        El impacto turístico de Vinicunca es bastante reciente, pero la historia de la montaña y sus
                        colores, empezó hace millones de años.

                    </p> <br>

                    <img src="{{ asset('img/blog/Vinicunca-portada.webp') }}" alt="Vinicunca" class="img-fluid"
                        class="text-center" loading="lazy"><br><br><br>

                    <h2><u>¿Qué nos cuenta la historia de Vinicunca?</u></h2>
                    <p class="p-tours">
                        Antiguamente los incas, solían denominar como “Apu” a algunas montañas, y les adjudicaron
                        poderes místicos, teniéndoles un gran respeto. <br>
                        La mayoría de estos majestuosos montes estaban cubiertos de nieve, entre ellos uno de los que
                        más destacó desde siempre, fue Vinicunca “La montaña de Colores”, que forma parte del nevado
                        Ausangate en la cordillera del Vilcanota. <br>
                        Según la historia, los Incas le conferían a Vinicunca un carácter masculino, porque se consideró
                        que sus cimientos brotaban de una energía masculina, la misma que fertilizaba y daba fuerza a la
                        Pachamama. <br>El distrito que acoge esta maravilla natural es Pitumarca, en la provincia de
                        Canchis. Los habitantes del lugar consideran que Vinicunca es una “montaña sagrada”, por lo que
                        realizan habitualmente diversos rituales, como el pago a la tierra, lo que puede hacer de la
                        visita una experiencia única y de gran misticismo.
                    </p> <br>


                    <img src="{{ asset('img/blog/montaña-vinicunca.webp') }}" alt="Vinicunca - Montaña de colores"
                        class="img-fluid" class="text-center" loading="lazy"><br> <br><br>

                    <h2><u>Explicación de la belleza y formación de la montaña de colores
                        </u></h2>
                    <p class="p-tours">
                        <i>El aspecto de arcoíris de Vinicunca se debe
                            a "una compleja historia geológica de sedimentos marinos, lacustres y fluviales"</i> , según
                        un
                        informe de la Oficina de Paisaje Cultural de la Dirección Desconcentrada de Cultura de
                        Cusco. <br>
                        Estos sedimentos, transportados por el agua que antes cubría la zona, datan de entre los
                        periodos terciario y cuaternario, es decir, de hace unos 65 a dos millones de años.
                        A lo largo del tiempo, los sedimentos fueron formando capas (con distintos tamaños de
                        granos) que hoy se ven como las franjas que tanto atraen a los turistas.
                        El movimiento de las placas tectónicas del área elevó estos sedimentos hasta que se
                        convirtieron en montañas.<br>
                        Luego fueron adquiriendo sus colores llamativos a causa a la oxidación de sus minerales,
                        ejercida por la humedad de la zona, y a la erosión de los mismos.
                    </p> <br>

                    <img src="{{ asset('img/blog/colores-montaña-vinicunca.webp') }}"
                        alt="Vinicunca - Montaña de colores" class="img-fluid" class="text-center"
                        loading="lazy"><br> <br>
                    <h2>Composicion de colores de Vinicunca (Montaña de colores)</h2>
                    <ul>
                        <li><strong>Rosado o fucsia:</strong> mezcla de arcilla roja, fangolitas (fango) y arena. </li>
                        <li><strong>Blanquecino:</strong> arenisca (arena de cuarzo) y piedra caliza, ricos en carbonato
                            de calcio.</li>
                        <li><strong>Morado o lavanda: </strong> (mezcla de arcilla y carbonato de calcio) y silicatos.
                        </li>
                        <li>
                            <strong>Rojo:</strong> por compuesto por las arcilitas (hierro) y arcillas pertenecientes al
                            terciario superior.
                        </li>
                        <li><strong>Verde: </strong> ricas en ferro magnesiano (mezcla de hierro y magnesio) y óxido de
                            cobre.</li>
                        <li><strong>Pardos amarillentos, mostaza o dorado: </strong>, areniscas calcáreas ricas en
                            minerales sulfurados (combinados con azufre).</li>
                    </ul>
                    <p class="p-tours">
                        Pero, si estos colores llamativos llevan millones de años tiñiendo la
                        montaña, <strong>¿Por qué se hizo
                            famosa hace relativamente poco tiempo?</strong>
                    </p>

                    <p class="p-tours">
                        Los pobladores de Pitumarca -según la municipalidad de este distrito- cuentan que había nieve
                        hace unos 70 años. <br>
                        Explican que la montaña ganó popularidad gracias a la promoción de los turistas que pasaban
                        caminando por el lugar en dirección al Ausangate, un nevado sagrado para los cusqueños.
                        Diversas revistas atribuyeron el éxito del fenómeno y popularidad de la montaña a los usuarios
                        de Instagram y Facebook.<br>
                        <i>Las imágenes que se muestran (en la red social) están llevando a que la gente se embarque en
                            un avión para replicar la foto...</i> <br><br>

                        <img src="{{ asset('img/blog/paisaje-vinicunca.webp') }}" alt="Montaña Vinicunca"
                            class="img-fluid" class="text-center" loading="lazy"><br>

                    <h2> <u>Rutas hacia la Montaña Vinicunca
                        </u></h2>
                    <p class="p-tours">
                        Si no sabes cómo llegar a la Montaña de 7 Colores o Arcoíris, te contamos cómo puedes hacerlo.
                        Nuestra recomendación es que la visites después de llevar varios días en Cusco para estar
                        aclimatado a la altitud, y no tener problemas con el mal de altura. <br> Piensa que la Montaña
                        de 7
                        Colores es el punto más alto en el que vas a estar en tu ruta por Perú (y en muchos casos, en tu
                        vida), así que cuanto más tiempo haya pasado, mejor. <br>

                        Para poder llegar a la montaña de siete colores, existe dos rutas: Pitumarca y Cusipata.<br>
                        <br>
                    <h3>Ruta Pitumarca:</h3>
                    <p class="p-tours">
                        Pitumarca es una de las rutas que te llevará a la montaña de siete colores, partirás desde el
                    Cusco, tomando transporte público de Huayruropata (cerca al Coliseo Cerrado del Cusco), el costo
                    del pasaje es económico, con dirección a Checacupe, pasarás la ruta por Andahuayllillas,
                    Quiquijana y finalmente llegarás a Cusipata.<br> Cabe resaltar que esta ruta es bastante exigente,
                    para el cual es necesario que este físicamente preparado.<br>
                    Las cuatrimotos, son una buena opción para algunas personas que elijan llegar a la Montaña de
                    siete colores, por la ruta de Pitumaca. El alquiler de las cuatrimotos, está aproximadamente 100
                    dólares, el alquiler es opcional y dependerá de que empresa las alquile.<br><br>
                    </p>

                    <h3>Ruta Cusipata:</h3>
                    <p class="p-tours">
                        Está ruta es reciente y menos exigente para los visitantes a la Montaña de Siete Colores, al igual
                    que en la ruta Pitumarca, partirás de Cusco, rumbo a Cusipata hasta llegar a Phuluwasipata, lugar
                    donde empezarás la caminata, recuerda que ascenderá aproximadamente 100 kilómetros desde la ciudad
                    del Cusco, y ascenderás la cumbre altitudinal situada a 5,200 metros sobre el nivel del mar
                    (m.s.n.m). La ruta Cusipata, tiene una opción para los visitantes que sientan que no puedan
                    continuar con la caminata. <br><br>

                    Estas son las dos diferentes formas de poder llegar a Vinuncunca, la maravillosa montaña de siete
                    colores, tu elijes, de acuerdo a tus costos, sin embargo te recomendamos contar con el servicio de
                    una agencia, porque este te garantizará una nueva aventura, a precios más cómodos, y no tendrás de
                    que preocuparte, solo disfrutar de tu caminata.<br>
                    Recuerda que puedes buscar la agencia que mejor te convenga, pero no olvides que está agencia debe
                    contar con la Licencia de funcionamiento respectiva y además tenga una Constancia emitida por la
                    Dirección Regional de Comercio Exterior y Turismo del Cusco (DIRCETUR), para garantizar su
                    seguridad.
                    </p>
                    </p> <br><br><br>
                    <h5 class="h5-tours text-center"> <u>Experiencia de Caminata hacia Vinicunca
                        </u></h5>
                    <p class="p-tours">
                        El trekking por la Montaña de 7 Colores es de dificultad alta y bastante duro, ya que se realiza
                        a mucha altitud, alcanzando los 5.200 metros aproximadamente. <br>Pero, si requiere de ayuda
                        tiene
                        diversas opciones: <br><br>
                        • Puedes subir a caballo hasta prácticamente la cima de la montaña Arcoíris con un costo
                        adicional. <br>

                        • Durante el recorrido hasta la Montaña de 7 Colores hay gente vendiendo bebidas, fruta, etc, no
                        hace falta llevar una mochila muy recargada.<br>

                        • También hay baños por todo el camino, ante cualquier emergencia. <br>
                        Ya sólo caminar entre valles alucinantes y montañas de colores que se encuentran a lo largo de
                        los 5 km hasta llegar a la Montaña Arco Iris, la caminata merece la pena.<br>
                        No sólo la Montaña de los 7 Colores es un espectáculo, sino Tanto o más, todo el camino para
                        llegar hasta ella.<br>
                        Y la gente que lleva los caballos, con trajes tradicionales hechos de alpaca y con colores
                        intensos, es también parte de esa estampa que parece un documental. Genial mires donde
                        mires...<br> <br>

