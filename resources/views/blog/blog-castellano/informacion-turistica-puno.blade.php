                    <h2 class="h5-tours">Historia. de Puno.</h2>
                    <p>
                        La región de Puno fue el territorio de las culturas Pucará y Tiahuanaco. En la actualidad hay
                        innumerables vestigios que demuestran la temprana ocupación de Puno por parte de estas y otras
                        culturas que sucedieron a la gran cultura Inca. Con el pasar de los años, Puno se fue
                        convirtiendo en un pueblo prospero a orillas del Lago Titicaca. Y su población estaba compuesta
                        de qollas, aymaras y quechuas.<br><br>

                        Años más tarde, según algunas leyendas y relatos mitológicos, algunos de los antiguos habitantes
                        de Puno migraron hacia el oeste y dieron origen a una de las culturas más grandes de América del
                        Sur: la cultura inca.<br><br>

                        Durante la colonia, la ciudad de Puno se caracterizaba por ser un pueblo modesto. Pero el 4 de
                        noviembre de 1668, el Virrey Conde de Lemos lo convirtió en la capital de la provincia de
                        Paucarcolla. A partir de entonces, dejó de llamarse San Juan Bautista de Puno y vino a
                        denominarse San Carlos de Puno.<br><br>

                        Sin embargo, la región Puno fue creada como región recién el 26 de abril de 1822.
                    </p>

