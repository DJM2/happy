                    <h2 class="h5-tours">El lago Titicaca.</h2>
                    <p class="p-tours">
                        El Lago Titicaca, es el lago de agua dulce más alto y navegable del mundo. Este Patrimonio
                        Natural está ubicado en la región de Puno y es compartido con el vecino país de Bolivia. Dentro
                        del Lago Titicaca existen majestuosas islas que son habitados por pescadores y agricultores. Las
                        islas más importantes, donde los pobladores locales aún practican sus ancestrales costumbres,
                        son: las Islas flotantes de los Uros, la Isla de Taquile y Amantaní.<br><br>

                        Alrededor del místico y sagrado Lago Titicaca están asentados milenarios pueblos. Los habitantes
                        de estos pueblos principalmente se sustentan con los ingresos que reciben del turismo.
                        Actualmente el turismo en el Lago Titicaca va en aumento y los numerosos grupos de turistas que
                        visitan estas islas provienen de todo el mundo en busca de explorar nuevas culturas. Todos estos
                        intrépidos viajeros a diario navegan por las frías aguas de color azul del Lago Titicaca hasta
                        alcanzar estas hermosas islas flotantes.<br><br>

                        Según la mitología andina, de las prístinas aguas del lago Titicaca emergieron los fundadores
                        del imperio inca, Manco Capac y Mama Occllo.
