                    <h2> ¿Cómo Llegar?</h2>

                    <p> Desde la ciudad de cusco, tomamos nuestra movilidad con dirección al sureste por la carretera
                        asfaltada y recorremos 108 km por un promedio de 2 horas hasta llegar a PAUCARTAMBO, donde
                        observaremos el puente de Carlos III, el templo, y dentro de este, a la Virgen del Carmen, que
                        es muy venerada con música y danzas en su festividad que empieza el 15 de julio hasta el 18 de
                        julio. <br><br>
                        Desde Paucartambo, continuaremos nuestro recorrido de 45 km por dos horas más, con dirección al
                        mirador de tres cruces de oro que se encuentra dentro del Parque Nacional del Manu que es una de
                        las reservas que alberga una exquisita biodiversidad reconocida a nivel mundial, actualmente
                        resguardada del impacto humano y fue nominada como patrimonio natural mundial en el año de 1987.
                    </p>

                    <h3> <strong>NOTA:</strong></h3>
                    <p>Antes de ir al mirador se debe de pagar un ticket de ingreso al Parque Nacional del Manu de 10.00
                        nuevos soles para adultos, y de 6.00 soles para niños.<br><br>
                        Después de tu experiencia en el mirador, puedes visitar por ejemplo la zona histórica cultural
                        “Pusharo” con un pago de ingreso extra de 50.00 soles, como también la zona del rio Manu con un
                        pago de ingreso de 150.00 soles y tendrás que ir acompañado con un guardaparque autorizado.
                    </p>



                    <h2> ¿Qué debes llevar?</h2>

                    <p>
                    <ul>
                        <li>Ropa abrigadora,ligera e impermeable.</li>
                        <li> Mochila.</li>
                        <li> Bloqueador solar y repelente.</li>
                        <li> Gorra o sombrero.</li>
                        <li> Agua y snacks.</li>
                        <li> Dinero en efectivo.</li>
                        <li> Cámara fotográfica.</li>
                    </ul>
                    <br><br>
                    </p>

                    <h2> ¿Cuál es la mejor época para ir?</h2>

                    <p> Se recomienda visitar el mirador entre los meses de Mayo a Julio; resaltando el 20, 21 y 22 de
                        junio ya que en estos días ocurre el solsticio de invierno y con suerte no amanece con nubes que
                        impiden tener unas vistas impresionantes; o también otra fecha imperdible es en la festividad de
                        la virgen del Carmen que empieza el 15 de julio y culmina el 18 de julio donde miles de devotos
                        se reúnen y presentan actividades artísticas, religiosas, culturales, y gastronómicas Mostrando
                        así sus costumbres y tradiciones a todo lugareño y visitante, no por nada Paucartambo es
                        reconocido como “capital folclórica del cusco”. </p>


                    <h2> ¿Te animas a ir a ver el amanecer?</h2>

                    <p>Esta experiencia es muy recomendada para quienes gustan de la naturaleza, y desean salir del
                        estrés de la ciudad ya sea en compañía de amigos y/o familia. <br><br>
                        El tiempo para contemplar este fenómeno natural es desde las 4.30 hasta las 5.30 de la mañana.
                        Los rayos de sol iluminan como caminos en el cielo húmedo por varios minutos, distorsionando la
                        imagen al dar una ilusión óptica de tres soles, como si cada sol saltara de un lado a
                        otro.<br><br>
                        Algunas agencias ofrecen un tour de dos días a Paucartambo. Iniciando muy temprano en la ciudad
                        del cusco con dirección a Paucartambo, y en transcurso del viaje hacer unas paradas para visitar
                        el complejo arqueológico de “Ninamarca” y sus chullpas preincas. <br> Al llegar al pueblo se visita
                        el templo de la Virgen del Carmen o el museo de las danzas típicas. El trekking hasta el mirador
                        de las tres cruces se lleva a cabo por la noche y se acampa en sitios autorizados y esperar un
                        amanecer espectacular que ilumina la densa selva del Manu.
                    </p>

