                        <o>Principales Atractivos</o>
                        <m> turisticos de Cusco </m>
                    </h2>
                    <div class="separador-naranja"></div>
                    <div class="separador-azul"></div>
                    <p class="p-tours">
                        Cusco, también conocida como Cuzco o Qosqo, La Capital Arqueológica de América, fue la principal
                        ciudad del Imperio Inca considerada por los mismos como “Ombligo del mundo” y actualmente, el
                        principal destino turístico del Perú, gracias a su gran legado de cultura, historia, y su gran
                        riqueza arquitectónica y espiritual.
                        Entre los principales atractivos turísticos de Cusco se encuentra Machu Picchu. Una joya
                        arquitectónica que tuvo como principal herramienta a gigantescas rocas.
                        También, podrás conocer sus espléndidos paisajes naturales, ruinas y valles. Incluso, una
                        variedad de costumbres antiguas, que aún se encuentran intactas.
                        <br> <br>

                        <img src="{{ asset('img/blog/ciudad-de-cusco.webp') }}" alt="Ciudad del Cusco" loading="lazzy"
                            width="100%">
                        <i> FOTO: Plaza de Armas del Cusco</i> <br> <br>

                    </p><br>

                    <h2> ¿Por qué visitar Cusco?</h2>

                    <p> Cusco es fascinante por ser un destino en el que encontrará, historia, naturaleza, aventura,
                        arte, gastronomía y costumbres que se mantienen vivas.

                    <h2> ¿Cuáles son los principales atractivos turísticos en Cusco?</h2>

                    Le brindamos información acerca de cuales son los más sorprendentes e interesantes lugares para
                    recorrer durante tu estadía en la ciudad imperial. <br><br>
                    </p>

                    <h2> Centros Arqueológicos</h2>

                    <p> La técnica constructiva inca, era completamente diferente a todo lo encontrado en otras
                        latitudes del mundo, por lo que es única en sus diversos centros arqueológicos. Grandes piedras,
                        algunas de más de cien toneladas, fueron acarreadas desde las canteras a más de 60 kilómetros
                        para su construcción. La orientación de sus puertas y ventanas, así como lo espectacular de su
                        construcción, son sólo algunos de los detalles que valorarán en este recorrido por estos centros
                        arqueológicos. </p>

                    <h5 class="h5-tours">&#9679; <u> SAQSAYHUAMAN: </u> </h5>
                    <p class="p-tours">
                        Sacsayhuamán es una de las edificaciones más asombrosas de todo el mundo. Se piensa que empezó a
                        construirse durante el gobierno del inca Pachacutec en el siglo XV. <br>
                        Se cree que más de 20 mil hombres extrajeron las piedras de las canteras aledañas y la
                        trasladaron 20 kilómetros hasta la colina de la ciudad del Cusco. <br> Hoy, se calcula que
                        Sacsayhuaman conserva solo el 40 por ciento de su antigua estructura. Aun así, el sitio posee
                        estructuras de hasta 125 toneladas de peso. <br><br>¿Cómo los incas construyeron Sacsayhuaman
                        con
                        estructuras de un peso y tamaño inverosímiles para la época? Muchas preguntas aún son un
                        misterio. Ahí reside la belleza de este sitio arqueológico.

                    </p>
                    <img src="{{ asset('img/blog/saqsayhuaman.webp') }}" alt="SAQSAYHUAMAN" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours"> &#9679;<u> CORICANCHA: </u></h5>
                    <p class="p-tours">
                        Posiblemente el edificio más sagrado e importante del imperio del Tahuantinsuyo fue el
                        Coricancha, también conocido como el Templo del Sol.<br> Durante el tiempo de la conquista, el
                        templo fue destruido y saqueado. Sobre su base, se construyó la iglesia y convento de Santo
                        Domingo. <br>Actualmente, existe un museo con todas las piezas incas que se hallaron allí
                        durante
                        las excavaciones.
                    </p>
                    <img src="{{ asset('img/blog/Coricancha.webp') }}" alt="Coricancha cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> QENQO: </u></h5>
                    <p class="p-tours">
                        El nombre quechua Qenqo significa ‘laberinto’. En la cosmovisión de la cultura andina, este
                        lugar fue construido para invocar al ‘kaypacha’, legendaria serpiente que dio origen a la vida.
                        Los incas dieron una gran importancia a ‘Kenko’ y sus túneles subterráneos pues creían que las
                        cuevas comunicaban el mundo del hombre con el mundo de los muertos.<br> Actualmente es uno de
                        los
                        recintos arqueológicos más visitados de la ciudad del Cusco.</p>
                    <br> <br>

                    <img src="{{ asset('img/blog/qenqo.webp') }}" alt="Qenqo" loading="lazzy" width="100%">
                    <br> <br>

                    <h5 class="h5-tours">&#9679;<u> TAMBOMACHAY: </u></h5>
                    <p class="p-tours">
                        El sitio arqueológico de Tambomachay, es junto con Qenqo, Puca Pucara y Sacsayhuaman, uno de los
                        complejos arqueológicos más importantes en la ciudad del Cusco. Se encuentra a solo 7 kilómetros
                        de la antigua capital de los incas. Debido a su ubicación, pertenece a uno de los centros
                        importantes en los ‘ceques’ o líneas imaginarias que partían del Cuzco y servían a los incas
                        para organizar sus santuarios y centros importantes. Su función fue la de proporcionar agua a
                        los sitios aledaños y como baños del inca. La armonía que existe entre sus muros de piedra y la
                        naturaleza que la rodea la transforman en uno de los lugares más importantes en el incanato.
                        Conoce algunos datos interesantes de este recinto arqueológico.</p> <br> <br>

                    <img src="{{ asset('img/blog/tambomachay.webp') }}" alt="Tabomachay Cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h2>Iglesias y Conventos.-</h2>

                    <p> La Iglesia Católica acompañó, desde los inicios de la conquista, a las tropas españolas. Al dar
                        inicio a su tarea evangelizadora, las órdenes sacerdotales de ese entonces construyeron sus
                        iglesias sobre los más importantes palacios y templos incas, para hacer gala de su poderio e
                        imponer su religion sobre los moradores cusqueños. <br>Por otro lado, tuvieron que construirse
                        conventos para albergar al clero. Las construcciones mejor conservadas tienen detalles
                        arquitectónicos e históricos dignos de ser apreciados.</p>

                    <h5 class="h5-tours">&#9679;<u> CATEDRAL DEL CUSCO: </u></h5>
                    <p class="p-tours">
                        La Catedral es uno de los atractivos turísticos más famosos del Cusco. Su arquitectura colonial
                        es fotografiada por casi todos los visitantes que llegan a la Plaza de Armas. Por dentro, el
                        templo ostenta cuadros, murales, esculturas y una colección de tesoros y antigüedades de mucho
                        valor. Se trata de no de los mayores repositorios de arte de la ciudad. <br>El ingreso tiene un
                        costo de 25 soles peruanos. También forma parte del famoso ‘City tour’ por el Cusco.</p> <br>
                    <br>

                    <img src="{{ asset('img/blog/catedral.webp') }}" alt="Catedral del Cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> IGLESIA Y CONVENTO DE SANTO DOMINGO: </u></h5>
                    <p class="p-tours">
                        Luego del saqueo del templo del Coricancha por los españoles en el siglo XVI, como sucedió con
                        todos los templos incas, una orden religiosa se encargó de la construcción de un templo
                        religioso católico para sustituir los dioses incas por los de occidente.<br><br>
                        La iglesia de Santo Domingo del Cusco sufrió severos daños tras el terremoto de 1650. Lo mismo
                        sucedió con el terremoto de 1950. <br>Las reconstrucciones demoraron varios años hasta tomar su
                        forma actual en 3 naves con una cúpula y variados adornos de madera, oro y plata.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/santo-domingo.webp') }}" alt="Convento Santo Domingo Cusco"
                        loading="lazzy" width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> IGLESIA DE SAN BLAS: </u></h5>
                    <p class="p-tours">
                        La iglesia de San Blas es una de las edificaciones más representativas de la iglesia católica.
                        Se encuentra en el barrio de San Blas, más conocido como el barrio de los artesanos a
                        aproximadamente 10 minutos de la Plaza de Armas de Cusco. <br>Conocer la Iglesia y Barrio de San
                        Blas, es una opción mientras te aclimatas en Cusco.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/san-blas.webp') }}" alt="Iglesia San Blas Cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> IGLESIA DE LA COMPAÑÍA DE JESÚS: </u></h5>
                    <p class="p-tours">
                        A un lado de la Plaza de Armas del Cusco destaca la imponente iglesia de la Compañía de Jesús,
                        uno de los templos cristianos construidos durante la época colonial. Como muchos monumentos
                        coloniales, este templo fue edificado sobre el antiguo Amarucancha (palacio del inca Huayna
                        Cápac). <br>Actualmente la iglesia forma parte del ‘Circuito del Barroco Andino’, una visita por
                        los
                        templos barrocos del Cusco que también incluyen la Iglesia de San Pedro Apóstol de
                        Andahuaylillas, el templo de San Juan Bautista de Huaro y la capilla de la Virgen Purificada de
                        Canincunca.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/compañia-de-jesus.webp') }}" alt="Compañia de Jesus Cusco"
                        loading="lazzy" width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> IGLESIA DE SAN CRISTÓBAL: </u></h5>
                    <p class="p-tours">
                        La iglesia de San Cristóbal de Cusco es uno de los templos cristianos más famosos, fue
                        construido durante la colonia sobre un solar de origen inca donado por Cristóbal Paullo Inca,
                        hermano de los ex gobernantes Manco Inca, Atahualpa y Huáscar. <br>Además de su valor histórico,
                        resalta por su ubicación en lo alto de la ciudad del Cusco, entre el camino de la Plaza de Armas
                        y Sacsayhuaman.<br> El templo es sencillo, posee una torre de piedra y el resto de la nave (en
                        forma
                        de cruz) hecho de adobe.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/Templo-De-San-Cristobal-Cusco.webp') }}"
                        alt="Templo de San Cristobal" loading="lazzy" width="100%"> <br> <br>

                    <h2>Museos.-</h2>

                    <p> Una ciudad, tan visitada como el Cusco, no podía carecer de museos. Estos, orientados a mostrar
                        diversos aspectos de la cultura Pre-inca, inca, colonial y republicana, se distribuyen a lo
                        largo de la ciudad. <br>Aventúrese a conocerlos, luego de su visita a ellos no dejará de
                        agradecérnoslo.</p>

                    <h5 class="h5-tours">&#9679;<u> MUSEO INKA: </u></h5>
                    <p class="p-tours">
                        El Museo Inka tiene una importancia histórica muy relevante porque es aquí donde en el incanato
                        fue casa de Huáscar, emperador del Tahuantinsuyo. También es conocido como la Casa del Almirante
                        porque fue construido en el siglo XVII por el almirante Alderete Maldonado. <br> Esa estructura
                        se
                        conserva hasta el día de hoy pese a que en 1950 fue dañada por un terremoto. Sin embargo, en
                        1952 se demolió parte del establecimiento para luego restaurarlo. En el proceso se perdieron
                        piezas originales y un importante número de artesonados. <br> Los tesoros que este museo
                        custodia
                        datan de la época preínca hasta el siglo XX. La historia de Cusco se resume allí.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/museo inca.webp') }}" alt="Museo Inka Cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> MUSEO DE ARTE PRECOLOMBINO: </u></h5>
                    <p class="p-tours">
                        El Museo de Arte Precolombino fue una kancha (patio) ceremonial en la época de los incas, en
                        1580 se convertiría en la mansión del conquistador Alonso Díaz y en 1850 en la del conde
                        Cabrera.<br> La residencia fue restaurada para ser el “Museo de arte Precolombino o Museo de
                        arte
                        Prehispánico” desde junio de 2003. Es el único museo dedicado a recuperar al arte de las
                        culturas del antiguo Perú.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/museo-precolombino.webp') }}"
                        alt="Museo de arte precolombino Cusco" loading="lazzy" width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> MUSEO CASA CONCHA: </u></h5>
                    <p class="p-tours">
                        El Museo Machu Picchu de la Casa Concha fue construido sobre el palacio del inca Túpac Yupanqui.
                        Exhibe cientos de artefactos extraídos por el explorador estadounidense Hiram Bingham en 1911.
                        Estos objetos fueron devueltos a Perú en 2011. <br> Hoy, este pequeño pero fascinante museo es
                        una de
                        las mejores opciones antes de su viaje a Machu Picchu.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/casa-concha.webp') }}" alt="Casa Concha Cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h2>Mercado Central de San Pedro</h2>
                    <p> El mercado de San Pedro es uno de los lugares más pintorescos de la ciudad del Cusco. Allí se
                        congregan cientos de ciudadanos cusqueños y turistas extranjeros para adquirir diversos
                        productos alimenticios así como artesanías de todo tipo.<br> Es una gran oportunidad para
                        conocer un
                        poco más la cultura del Cusco. Se encuentra en el centro histórico de la ciudad, a pocos pasos
                        de la Plaza de Armas.<br><br>
                        Actualmente, allí existen 1180 tienda que comercializan frutas, verduras, panes, golosinas,
                        utensilios, desayunos, almuerzos así como toda clase de artesanías.<br>
                        Debido a su ubicación en la zona comprendida por el Centro Histórico del Cusco, allí se reúnen
                        no solo ciudadanos cusqueños sino también muchos turistas provenientes de distintas partes del
                        mundo. Por lo tanto, el mercado se convirtió en un espacio de culturización entre distintas
                        personas.<br><br>
                        El mercado San Pedro también es una oportunidad para el turista que desea adquirir cualquier
                        producto a un precio económico. Por ejemplo, el precio de un menú (almuerzo con entrada) es de
                        solo 6 soles peruanos (1,5 dólares americanos, aproximadamente).</p>

                    <img src="{{ asset('img/blog/mercado-san-pedro.webp') }}"
                        alt="Mercado central de San Pedro Cusco" loading="lazzy" width="100%"> <br> <br>

                    <p> Ven a disfrutar de la calidez de un pueblo que disfruta con los visitantes, Cusco espera por ti.
                        ¡No puedes perder la oportunidad de conocerla!</p>




