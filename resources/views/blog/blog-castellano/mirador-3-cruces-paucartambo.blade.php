                    <p class="p-tours">

                        Paucartambo es un distrito de Cusco localizado a 2900 m.s.n.m, con una variedad de flora, fauna
                        y costumbres ancestrales. <br>
                        En el mirador de tres cruces con su bosque nuboso a 3850m.sn.n.m es posible observar un
                        espectáculo natural, contemplando colores y tonalidades en el cielo, la luz y como va saliendo
                        el sol de una forma muy diferente a lo que podemos estar acostumbrados, este fenómeno se observa
                        solo en 2 lugares en el mundo: Paucartambo y Japón, por consiguiente esta experiencia siempre la
                        llevaras en tu mente como algo surreal.<br><br>
                        A la vez desde este mirador se puede observar la cadena de montañas del nevado Ausangate que
                        contrasta con el rio Paucartambo.

                        <br> <br>

