                    <h2 class="h2-happy">
                        <o>Os principais pontos </o>
                        <m> turísticos de Cusco </m>
                    </h2>
                    <div class="separador-naranja"></div>
                    <div class="separador-azul"></div>
                    <p class="p-tours">
                        Cusco, também conhecida como Cuzco ou Qosqo, a Capital Arqueológica da América, foi a principal
                        cidade do Império Inca considerada por eles como "Umbigo do mundo" e atualmente, o principal
                        destino turístico do Peru, graças ao seu grande legado cultural , história e sua grande riqueza
                        arquitetônica e espiritual.
                        Entre os principais atrativos turísticos de Cusco está Machu Picchu. Uma joia arquitetônica cuja
                        ferramenta principal eram rochas gigantescas.
                        Também poderá conhecer suas esplêndidas paisagens naturais, ruínas e vales. Inclusive uma
                        variedade de costumes antigos, que ainda estão intactos.
                        <br> <br>

                        <img src="{{ asset('img/blog/ciudad-de-cusco.webp') }}" alt="Ciudad del Cusco" loading="lazzy"
                            width="100%">
                        <i> FOTO: Praça de Armas de Cusco</i> <br> <br>

                    </p><br>

                    <h2> Porquê visitar Cusco?</h2>

                    <p> Cusco é fascinante por ser um destino onde se encontra história, natureza, aventura, arte,
                        gastronomia e costumes que se mantêm vivos.
                    <h2> Quais são os principais pontos turísticos de Cusco?</h2>
                    Fornecemos a você informações sobre quais são os lugares mais surpreendentes e interessantes para
                    visitar durante sua estadia na cidade imperial. <br><br>
                    </p>
                    <h2> Centros Arqueológicos</h2>

                    <p> A técnica de construção inca era completamente diferente de tudo o que se encontra em outras
                        latitudes do mundo, por isso é única em seus vários centros arqueológicos. Pedras grandes,
                        algumas pesando mais de cem toneladas, foram transportadas de pedreiras com mais de 60
                        quilômetros para construção. A orientação das suas portas e janelas, bem como a sua espectacular
                        construção, são apenas alguns dos pormenores que irá valorizar nesta visita a estes centros
                        arqueológicos. </p>

                    <h5 class="h5-tours">&#9679; <u> SAQSAYHUAMAN: </u> </h5>
                    <p class="p-tours">
                        Sacsayhuamán é uma das construções mais incríveis do mundo. Acredita-se que começou a ser
                        construída durante o governo do Inca Pachacutec no século XV. <br>
                        Acredita-se que mais de 20.000 homens extraíram as pedras das pedreiras próximas e as
                        transportaram 20 quilômetros colina acima da cidade de Cusco. <br> Hoje, estima-se que
                        Sacsayhuaman retém apenas 40 por cento de sua estrutura antiga. Mesmo assim, o local conta com
                        estruturas de até 125 toneladas de peso. <br><br>Como os Incas construíram Sacsayhuaman com
                        estruturas de tamanho e peso inacreditáveis para a época? Muitas questões ainda
                        misterioso. Nisso reside a beleza deste sítio arqueológico.

                    </p>
                    <img src="{{ asset('img/blog/saqsayhuaman.webp') }}" alt="SAQSAYHUAMAN" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours"> &#9679;<u> CORICANCHA: </u></h5>
                    <p class="p-tours">
                        Possivelmente, o edifício mais sagrado e importante do império Tahuantinsuyo foi o Coricancha,
                        também conhecido como o Templo do Sol.<br> Durante a época da conquista, o templo foi destruído
                        e saqueado. Em sua base foi construído a igreja e convento de Santo Domingo. <br>Atualmente, há
                        um museu com todas as peças incas que foram encontradas lá durante as escavações.
                    </p>
                    <img src="{{ asset('img/blog/Coricancha.webp') }}" alt="Coricancha cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> QENQO: </u></h5>
                    <p class="p-tours">
                        O nome quéchua Qenqo significa "labirinto". Na cosmovisão da cultura andina, este lugar foi
                        construído para invocar a 'kaypacha', uma serpente lendária que deu vida.
                        Os incas deram grande importância ao 'Kenko' e seus túneis subterrâneos, pois acreditavam que as
                        cavernas comunicavam o mundo do homem com o mundo dos mortos.<br> Atualmente é um dos sítios
                        arqueológicos mais visitados da cidade de Cusco.</p>
                    <br> <br>

                    <img src="{{ asset('img/blog/qenqo.webp') }}" alt="Qenqo" loading="lazzy" width="100%">
                    <br> <br>

                    <h5 class="h5-tours">&#9679;<u> TAMBOMACHAY: </u></h5>
                    <p class="p-tours">
                        O sítio arqueológico de Tambomachay, junto com Qenqo, Puca Pucara e Sacsayhuaman, é um dos mais
                        importantes complexos arqueológicos da cidade de Cusco. são apenas 7 km
                        da antiga capital inca. Devido à sua localização, pertence a um dos centros importantes nos
                        'ceques' ou linhas imaginárias que partiam de Cuzco e serviam aos incas para organizar seus
                        santuários e importantes centros. Sua função era fornecer água para os locais ao redor e como
                        banhos incas. A harmonia que existe entre suas paredes de pedra e a natureza que a cerca faz com
                        que seja um dos lugares mais importantes dos Incas.
                        Conheça algumas curiosidades sobre este sítio arqueológico.</p> <br> <br>

                    <img src="{{ asset('img/blog/tambomachay.webp') }}" alt="Tabomachay Cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h2>Igrejas e Conventos.-</h2>

                    <p> A Igreja Católica acompanhou as tropas espanholas desde o começo da conquista. No início de sua
                        tarefa evangelizadora, as ordens sacerdotais da época construíram suas igrejas em cima dos mais
                        importantes palácios e templos incas, para exibir seu poder e impor sua religião aos habitantes
                        de Cusco. <br>Por outro lado, os conventos tiveram de ser construídos para abrigar o clero. As
                        construções mais bem preservadas têm detalhes arquitetônicos e históricos dignos de serem
                        apreciados.</p>

                    <h5 class="h5-tours">&#9679;<u> CATEDRAL DEL CUSCO: </u></h5>
                    <p class="p-tours">
                        A Catedral é um dos pontos turísticos mais famosos de Cusco. Sua arquitetura colonial é
                        fotografada por quase todos os visitantes que visitam a Plaza de Armas. No interior, o templo
                        possui pinturas, murais, esculturas e uma coleção de tesouros e antiguidades valiosos. Não é um
                        dos maiores depósitos de arte na cidade. <br>A entrada tem um custo de 25 soles peruanos. Também
                        faz parte do famoso "City tour" por Cusco.</p> <br>
                    <br>

                    <img src="{{ asset('img/blog/catedral.webp') }}" alt="Catedral del Cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> IGREJA E CONVENTO DO SANTO DOMINGO: </u></h5>
                    <p class="p-tours">
                        Após o saque do templo de Coricancha pelos espanhóis no século XVI, como aconteceu com todos os
                        templos incas, uma ordem religiosa se encarregou da construção de um templo religioso católico
                        para substituir os deuses incas pelos do ocidente.<br><br>
                        A igreja de Santo Domingo del Cusco sofreu graves danos após o terremoto de 1650. A mesma coisa
                        aconteceu com o terremoto de 1950. <br>As reconstruções levaram vários anos para
                        forma actual em 3 naves com cúpula e várias decorações em madeira, ouro e prata.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/santo-domingo.webp') }}" alt="Convento Santo Domingo Cusco"
                        loading="lazzy" width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> IGREJA SAN BLAS: </u></h5>
                    <p class="p-tours">
                        A igreja de San Blas é uma das construções mais representativas da igreja católica.
                        Está localizado no bairro de San Blas, mais conhecido como bairro dos artesãos, a cerca de 10
                        minutos da Plaza de Armas de Cusco. <br>Conhecer a Igreja e Bairro de San Blas, é uma opção
                        enquanto você se aclimata em Cusco.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/san-blas.webp') }}" alt="Iglesia San Blas Cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> IGREJA DA LA COMPAÑÍA DE JESÚS: </u></h5>
                    <p class="p-tours">
                        De um lado da Plaza de Armas em Cusco se destaca a imponente igreja da Compañía de Jesús, um dos
                        templos cristãos construídos durante a época colonial. Como muitos monumentos coloniais, este
                        templo foi construído sobre o antigo Amarucancha (palácio do Inca Huayna Cápac). <br>Atualmente
                        a igreja faz parte do 'Circuito Barroco Andino', uma visita aos templos barrocos de Cusco que
                        também incluem a Igreja de San Pedro Apóstol de Andahuaylillas, o templo de San Juan Bautista de
                        Huaro e a capela da Virgem Purificado de Canincunca.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/compañia-de-jesus.webp') }}" alt="Compañia de Jesus Cusco"
                        loading="lazzy" width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> IGREJA DA SAN CRISTÓBAL: </u></h5>
                    <p class="p-tours">
                        A igreja de San Cristóbal de Cusco é um dos mais famosos templos cristãos, foi construída
                        durante a colônia em um local de origem inca doado por Cristóbal Paullo Inca, irmão dos antigos
                        governantes Manco Inca, Atahualpa e Huáscar. <br>Além de seu valor histórico, destaca-se por sua
                        localização no alto da cidade de Cusco, entre o caminho da Plaza de Armas e Sacsayhuaman.<br> O
                        templo é simples, possui uma torre de pedra e o resto do navio (em
                        em forma de cruz) em adobe.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/Templo-De-San-Cristobal-Cusco.webp') }}" alt="Templo de San Cristobal"
                        loading="lazzy" width="100%"> <br> <br>

                    <h2>Museus.-</h2>

                    <p> Uma cidade, tão visitada como Cusco, não poderia faltar museus. Estes, com o objetivo de mostrar
                        vários aspectos da cultura pré-inca, inca, colonial e republicana, estão distribuídos por toda a
                        cidade. <br>Aventure-se a conhecê-los, depois de sua visita a eles você não vai parar de nos
                        agradecer.</p>

                    <h5 class="h5-tours">&#9679;<u> MUSEO INKA: </u></h5>
                    <p class="p-tours">
                        O Museu Inka tem uma importância histórica muito relevante porque é aqui que Huáscar, imperador
                        de Tahuantinsuyo, foi abrigado pelos Incas. Também é conhecida como a Casa do Almirante
                        pois foi construído no século XVII pelo Almirante Alderete Maldonado. <br> Esta estrutura é
                        preservada até hoje, apesar de ter sido danificada por um terremoto em 1950. No entanto, em 1952
                        parte do estabelecimento foi demolido e depois restaurado. No processo perderam-se peças
                        originais e um número significativo de caixotões. <br> Os tesouros que este museu guarda datam
                        da época pré-incaica ao século XX. A história de Cusco está resumida lá.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/museo inca.webp') }}" alt="Museo Inka Cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> MUSEU DE ARTE PRECOLOMBINO: </u></h5>
                    <p class="p-tours">
                        O Museu de Arte Pré-Colombiana era um kancha cerimonial (pátio) no tempo dos Incas, em 1580 se
                        tornaria a mansão do conquistador Alonso Díaz e em 1850 a do Conde Cabrera.<br> A residência foi
                        restaurada para ser o “Museu de Arte Pré-Colombiana ou Museu de Arte Pré-Hispânica” desde junho
                        de 2003. É o único museu dedicado a recuperar a arte das culturas do antigo Peru.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/museo-precolombino.webp') }}" alt="Museo de arte precolombino Cusco"
                        loading="lazzy" width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> MUSEU CASA CONCHA: </u></h5>
                    <p class="p-tours">
                        O Museu Casa Concha Machu Picchu foi construído sobre o palácio do Inca Túpac Yupanqui.
                        Ele exibe centenas de artefatos removidos pelo explorador americano Hiram Bingham em 1911.
                        Esses objetos foram devolvidos ao Peru em 2011. <br> Hoje, este pequeno, mas fascinante museu é
                        uma das melhores opções antes de sua viagem a Machu Picchu.
                    </p> <br>
                    <br>

                    <img src="{{ asset('img/blog/casa-concha.webp') }}" alt="Casa Concha Cusco" loading="lazzy"
                        width="100%"> <br> <br>

                    <h2>Mercado Central de San Pedro</h2>
                    <p> O mercado de San Pedro é um dos lugares mais pitorescos da cidade de Cusco. Centenas de cidadãos
                        de Cusco e turistas estrangeiros se reúnem lá para adquirir diversos produtos alimentícios, bem
                        como artesanato de todos os tipos.<br> É uma ótima oportunidade para aprender um pouco mais
                        sobre a cultura de Cusco. Está localizado no centro histórico da cidade, a poucos passos da
                        Plaza de Armas.<br><br>
                        Atualmente, são 1.180 lojas que vendem frutas, verduras, pães, doces, utensílios, cafés da
                        manhã, almoços, além de todo tipo de artesanato.<br>
                        Devido à sua localização na área compreendida pelo Centro Histórico de Cusco, ali se encontram
                        não apenas cidadãos de Cusco, mas também muitos turistas de diferentes partes do mundo. Assim, o
                        mercado tornou-se um espaço de aculturação entre diferentes pessoas.<br><br>
                        O mercado de San Pedro também é uma oportunidade para os turistas que desejam adquirir qualquer
                        produto a um preço acessível. Por exemplo, o preço de um menu (almoço com entrada) é
                        apenas 6 soles peruanos (aproximadamente 1,5 dólares americanos).</p>

                    <img src="{{ asset('img/blog/mercado-san-pedro.webp') }}"
                        alt="Mercado central de San Pedro Cusco" loading="lazzy" width="100%"> <br> <br>

                    <p> Venha desfrutar do calor de uma cidade que gosta de visitantes, Cusco espera por você.
                        Você não pode perder a chance de conhecê-la!</p>




