                    <h2 class="h2-happy">
                        <o>A requintada</o>
                        <m> Gastronomía Do Perú </m>
                    </h2>
                    <div class="separador-naranja"></div>
                    <div class="separador-azul"></div>
                    <p class="p-tours">
                        Em 2021, o Peru participou do World Travel Awards (WTA), conquistando a designação de melhor
                        destino cultural e culinário do mundo. <br>
                        Da mesma forma, em 23 de novembro de 2021, o restaurante peruano Central, dos renomados chefs
                        Virgilio Martínez e Pía León, foi reconhecido por sua exploração gastronômica da biodiversidade
                        do Peru e conquistou o primeiro lugar como melhor restaurante da América Latina. <br> <br>

                        <img src="{{ asset('img/blog/gastronomia-peruana.webp') }}" alt="Gastronomía Peruana"
                            loading="lazzy" width="100%"> <br> <br>

                        Não há dúvida de que o Peru está aos olhos do mundo, temos a grande sorte de ter 3 regiões bem
                        marcadas como a Costa, a Serra e a Selva e cada região é muito particular devido aos microclimas
                        que existem dentro eles, com a conseqüente produção agrícola de produtos variados que tornam a
                        gastronomia peruana muito diversificada. <br>
                        A seguir, deixamos uma lista de pratos que você não pode deixar de provar ao visitar o Peru.
                    </p><br>

                    <h5 class="h5-tours">&#9679; <u> CEBICHE </u> </h5>
                    <p class="p-tours">
                        Não está muito claro de onde vem o nome deste prato da bandeira peruana, talvez a coisa mais
                        próxima do nome seja "SIBECH" que na língua árabe significa "comida ácida ou cítrica".
                        Algumas investigações apontam que este prato foi consumido há 2000 anos no Peru, sendo a
                        civilização Tallan que se desenvolveu no que hoje conhecemos como a área de Piura as primeiras
                        pessoas a consumi-los, da mesma forma que essas investigações referem-se aos Mochicas como
                        comensais desta iguaria, sendo o prato preparado com peixe fresco e marinado com o suco
                        fermentado do tumbo, (um cítrico do norte do Peru, família do maracujá). <br>
                        Durante os tempos incas este prato também era consumido, mas com a diferença de que no altiplano
                        peruano era marinado com chicha, a bebida sagrada dos incas que vem da fermentação do milho. Com
                        a conquista dos espanhóis, o consumo deste prato em terras incas começa a ficar em evidência
                        escrita graças às suas crônicas, onde é mencionado que esta iguaria contém sal e pimenta em sua
                        preparação. <br> Com a presença dos espanhóis, o prato foi aprimorado, por isso são eles que
                        adicionam a cebola e as laranjas azedas para o seu preparo, que era semelhante à já mencionada
                        comida mediterrânea Sibech. <br>
                        Em meados do século 20, na cidade de Lima, os peruanos acrescentaram mais ingredientes para
                        tornar esta iguaria nosso prato representativo, acrescentando batata-doce, milho, alface, salsa
                        e
                        yuyo ou alga, da mesma forma a laranja azeda foi trocada pelo limão peruano para que o cozimento
                        do peixe seja mais rápido.
                    </p>
                    <img src="{{ asset('img/blog/cebiche-peruano.webp') }}" alt="Ceviche peruano" loading="lazzy"
                        width="100%"> <br> <br>

                    <h5 class="h5-tours"> &#9679;<u> AJÍ DE GALLINA </u></h5>
                    <p class="p-tours">
                        A história deste conhecido prato leva-nos à Espanha, onde durante o século XIV era muito comum
                        na sociedade consumir manjar blanco, uma sandes de peito de frango cozido, temperado com açúcar,
                        nozes e amêndoas, engrossado com farinha e arroz, que atualmente é consumida na gastronomia
                        espanhola, ao longo do tempo esta sobremesa sofreu algumas alterações para se tornar uma
                        sobremesa muito mais fria, deixando de lado o frango e acrescentando nozes e açúcar,
                        permanecendo ligeiramente doce para o paladar dos comensais.
                        <br> Nos tempos coloniais este prato entrou em terras peruanas e foi bem recebido pelos
                        comensais, o estranho é que não era mais uma sobremesa que era servida após o prato principal,
                        mas sim, tornou-se o prato principal devido ao seu sabor único, sem dúvida, alguns foram os
                        pratos mais pedidos por peruanos e espanhóis. <br> Com o passar do tempo, os peruanos começaram
                        a aprimorar ainda mais essa iguaria, a pimenta amarela foi incorporada ao preparo, o que lhe
                        confere uma cor amarelada e um sabor indiscutível, desta forma a sobremesa deixou de ser doce e
                        tornou-se um prato principal pedido no país.<br>
                        Segundo Rodolfo Tafur, pesquisador gastronômico, em 1839, esse prato se tornou tão popular que
                        não havia restaurante peruano que não o oferecesse, além disso, naquela época o prato era
                        preparado com pedaços de carne de frango cozida, cebola previamente frita na panela , alho
                        esmagado, pedacinhos de pão e, obviamente, a pimenta amarela para dar cor, tudo acompanhado de
                        arroz branco.
                    </p>
                    <img src="{{ asset('img/blog/aji-de-gallina.webp') }}" alt="Plato aji de gallina Perú"
                        loading="lazzy" width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> CAUSA RELLENA </u></h5>
                    <p class="p-tours">
                        Não há dúvida de que o Peru é a terra da batata, segundo dados estatísticos neste país são
                        cultivadas mais de 3500 variedades de batata, sendo o principal alimento da culinária peruana,
                        este tubérculo foi plantado pela primeira vez na parte sul do o Peru 5000 aC, aproximadamente,
                        hoje ainda existem comunidades andinas altas que preservam as antigas técnicas de plantio e
                        colheita do referido tubérculo, um claro exemplo disso é o parque de batata no departamento de
                        Cusco, província de Urubamba, distrito de Pisaq. <br>O Parque da Batata é um Território
                        Biocultural dedicado à conservação da diversidade biológica e cultural da Cordilheira dos Andes.
                        É administrado pelas cinco comunidades indígenas de Amaru, Chawaytire, Pampallacta, Paru Paru e
                        Sacaca, com o apoio da Associação pela Natureza e Desenvolvimento Sustentável, ANDES. <br>
                        O nome da causa recheada vem da palavra quecha "Kausay" que em espanhol significa "vida" nos
                        tempos incas basicamente era batatas cozidas com fatias de pimentão e geralmente era comida
                        quando se trabalhava nos locais de cultivo. Já nos tempos coloniais, os espanhóis acrescentavam
                        mais ingredientes como azeitonas, abacate, ovos, peixe, etc. <br>
                        Outra teoria vem da Guerra do Pacífico, onde o Chile estava lutando contra o Peru e a Bolívia,
                        diz-se que quando o exército peruano invadiu o Chile, não havia mais comida suficiente para os
                        soldados, é por isso que as mulheres que os acompanhavam se reuniam o pouco que eles tinham mais
                        do que suficiente
                        basicamente era batata, limão, pimenta, sal e pimenta eles misturaram tudo e saiu esse prato, na
                        hora de alimentar os soldados eles repetiam constantemente essa frase "pela causa".
                        <br> <br>

                        <img src="{{ asset('img/blog/causa-limeña.webp') }}" alt="Gastronomia causa limeña Perú"
                            loading="lazzy" width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> LOMO SALTADO </u></h5>
                    <p class="p-tours">
                        Este prato, ao contrário de outros, não tem origem na era inca, por isso não é nativo do Peru. A
                        origem deste prato vem da época colonial exatamente em 1538, quando a primeira carne bovina
                        chegou ao Peru, é então que os espanhóis trouxeram a carne frita como aperitivo, era um dos
                        pratos mais caros naquela época desde que a carne veio da Espanha para o Peru, com o passar do
                        tempo o preço da carne foi caindo e já estava mais acessível à população, então em 1849 com a
                        chegada dos chineses no Peru esse prato foi melhorando, os asiáticos começaram a fundir seus
                        condimentos e artes culinárias com
                        os ingredientes que estavam disponíveis na época e como resultado temos o famoso lomo saltado,
                        que é a fusão de 3 culturas em um único prato, já que tem carne bovina, cebola e tomate trazidos
                        da Espanha, o jeito do refogado os legumes com molho de soja, que é a marca da cozinha chinesa,
                        da mesma forma que temos o arroz trazido pelos chineses e como complemento final as batatas que
                        são nativas do Peru. <br>
                        Hoje em dia existem muitas opções para saborear este prato, em alguns restaurantes a carne de
                        frango é usada em vez de carne bovina, nas terras altas do Peru pode ser encontrada com alpaca
                        ou carne de cordeiro, e da mesma forma para algumas pessoas que são vegetarianas você pode
                        encontrá-los com carne de soja, mas sim, o tipo de carne pode variar, mas a maneira de
                        prepará-la será sempre a mesma, você não pode perder a essência da comida.<br> <br>

                        <img src="{{ asset('img/blog/lomo-saltado.webp') }}" alt="Palto tipico - lomo saltado Perú"
                            loading="lazzy" width="100%"> <br> <br>

                    <h5 class="h5-tours">&#9679;<u> CUY </u></h5>
                    <p class="p-tours">
                        No mundo existem apenas 3 lugares onde você pode comer este delicioso prato, esses países são
                        Bolívia, Equador e obviamente no Peru, segundo a história este prato era consumido desde os
                        tempos pré-incas, o que significa que as pessoas o consumiam entre 250 300 anos antes de Cristo,
                        na época dos incas este prato era uma iguaria que era consumida por pessoas da nobreza e
                        normalmente em datas importantes, da mesma forma que se sabe da história que os incas
                        sacrificavam esses roedores para poder fazer oferendas aos seus deuses, um exemplo disso está em
                        Machu Picchu, onde no templo do condor nas escavações que foram feitas, foram encontrados ossos
                        de cobaias, alpacas e lhamas. Não há dúvida de que este era um animal importante dentro da
                        religião andina. <br>
                        É muito incerto como esses roedores chegaram à Europa, Ásia ou América do Norte e por que não
                        são consumidos como carne nesses continentes, já que geralmente são animais de estimação para
                        famílias. Reza a história que quando Cristóvão Colombo descobriu a América, em sua viagem de
                        volta à Europa trouxe diversos tipos de presentes de terras americanas, como exemplos de terra,
                        pedras, plantas, etc. Da mesma forma, trouxe os porquinhos-da-índia que foram dado de presente
                        para as famílias que apoiaram financeiramente sua expedição, e é lá que naquela época apenas
                        algumas famílias ricas da Europa tinham um animal tão exótico que era impossível encontrar, daí
                        parte da razão pela qual eles começaram a ser criados como animais de estimação, pois se fossem
                        eles comiam, para que não pudessem exibir um animal tão raro para a época e lugar. Outras lendas
                        dizem que este animal foi trazido para a Europa pelos espanhóis após a conquista dos Incas em
                        1535, para ser dado de presente ao Rei da Espanha e toda a nobreza. <br>
                        Sabe-se que a carne de cobaia possui alto valor nutricional, pois é fonte de proteína animal e,
                        ao mesmo tempo, um alimento com baixo teor de gordura e colesterol. Como pode ser visto na
                        tabela, seu valor proteico é de 20,3%, superando o frango e a carne bovina, que possuem apenas
                        18,3% e 17,5% respectivamente, e também a carne suína, que atinge apenas 14,5% de proteína. Da
                        mesma forma, enquanto a cobaia tem apenas 7,8% de gordura, a carne bovina chega a 21,8% e a
                        suína 37,3%, o que confirma suas vantagens em relação às demais carnes. <br>
                        <strong>Existem diferentes formas de consumir, pode ser frito, assado, na sopa, grelhado
                            etc.</strong> <br> <br>

