                    <h2><u>O que nos diz a história da Vinicunca?</u></h2>
                    <p class="p-tours">
                        Antigamente os incas chamavam algumas montanhas de "Apu" e lhes conferiam poderes místicos,
                        tendo grande respeito por elas. <br>
                        A maioria dessas majestosas montanhas estavam cobertas de neve, entre elas uma das mais
                        destacadas de todos os tempos, era Vinicunca "A Montanha das Cores", que faz parte do Ausangate
                        nevado na serra de Vilcanota. <br>
                        Segundo a história, os Incas deram a Vinicunca um caráter masculino, pois se considerava que
                        suas bases brotavam de uma energia masculina, a mesma que fertilizou e deu força à Pachamama.
                        <br>O distrito que hospeda essa maravilha natural é Pitumarca, na província de Canchis. Os
                        moradores consideram que Vinicunca é uma "montanha sagrada",
                        Costumam realizar vários rituais, como o pagamento da terra, o que pode tornar a visita numa
                        experiência única e altamente mística.
                    </p> <br>


                    <img src="{{ asset('img/blog/montaña-vinicunca.webp') }}" alt="Vinicunca - Montaña de colores"
                        class="img-fluid" class="text-center" loading="lazy"><br> <br><br>

                    <h2><u>Explicação da beleza e da formação da montanha das cores
                        </u></h2>
                    <p class="p-tours">
                        <i>O aspecto de arco-íris de Vinicunca é devido a "uma história geológica complexa de sedimentos
                            marinhos, lacustres e fluviais"</i> , de acordo com um relatório do Escritório de Paisagem
                        Cultural da Diretoria Descentralizada de Cultura de Cusco. <br>
                        Esses sedimentos, transportados pelas águas que antes cobriam a área, datam entre os períodos
                        Terciário e Quaternário, ou seja, cerca de 65 a dois milhões de anos atrás.
                        Com o passar do tempo, os sedimentos formaram camadas (com diferentes tamanhos de grãos) que
                        hoje são vistas como as faixas que tanto atraem turistas.
                        O movimento das placas tectônicas na área elevou esses sedimentos até que se tornaram
                        montanhas.<br>
                        Em seguida, começaram a adquirir suas cores marcantes devido à oxidação de seus minerais,
                        exercida pela umidade da área, e sua erosão.
                    </p> <br>

                    <img src="{{ asset('img/blog/colores-montaña-vinicunca.webp') }}"
                        alt="Vinicunca - Montaña de colores" class="img-fluid" class="text-center" loading="lazy"><br>
                    <br>
                    <h2>Composição de cores da Vinicunca (Montanha das cores)</h2>
                    <ul>
                        <li><strong>Rosa ou fúcsia:</strong> mistura de barro vermelho, fangolitas (lama) e areia. </li>
                        <li><strong>Esbranquiçadas:</strong> arenito (areia de quartzo) e calcário, ricos em carbonato
                            de cálcio.</li>
                        <li><strong>Roxo ou alfazema: </strong> (mistura de argila e carbonato de cálcio) e silicatos.
                        </li>
                        <li>
                            <strong>Vermelho:</strong> para composto por argilitos (ferro) e argilas pertencentes ao
                            terciário superior.
                        </li>
                        <li><strong>Verde: </strong> rico em ferromagnésia (mistura de ferro e magnésio) e óxido de
                            cobre.</li>
                        <li><strong>Marrom amarelado, mostarda ou dourado: </strong>, arenitos calcários ricos em
                            minerais sulfurosos (combinados com enxofre).</li>
                    </ul>
                    <p class="p-tours">
                        Mas, se essas cores marcantes têm manchado a
                        montanha, <strong>Pelo que se tornou famoso recentemente?</strong>
                    </p>

                    <p class="p-tours">
                        Os moradores de Pitumarca -de acordo com o município deste distrito- dizem que havia neve cerca
                        de 70 anos atrás. <br>
                        Eles explicam que a montanha ganhou popularidade graças à promoção dos turistas que percorreram
                        o local em direção a Ausangate, uma montanha sagrada para o povo de Cusco.
                        Diversas revistas atribuíram o sucesso do fenômeno e a popularidade da montanha aos usuários do
                        Instagram e Facebook.<br>
                        <i>As imagens exibidas (na rede social) estão levando as pessoas a embarcar em um avião para
                            replicar a foto...</i> <br><br>

