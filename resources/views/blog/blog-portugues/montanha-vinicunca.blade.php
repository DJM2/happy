                    <h2> <u>Roteiros para a Serra do Vinicunca
                        </u></h2>
                    <p class="p-tours">
                        Se você não sabe como chegar à Montanha das 7 Cores ou ao Arco-Íris, nós te contamos como você
                        pode chegar.
                        Nossa recomendação é que você a visite depois de passar vários dias em Cusco para se acostumar
                        com a altitude e não ter problemas com o mal da altitude. <br> Pense que a Montanha das 7 Cores
                        é o ponto mais alto onde você estará em sua rota pelo Peru (e em muitos casos, em sua vida),
                        então quanto mais tempo tiver passado, melhor. <br>
                        Para chegar à montanha das sete cores, existem dois caminhos: Pitumarca e Cusipata.<br>
                        <br>
                    <h3>Rota Pitumarca:</h3>
                    <p class="p-tours">
                        Pitumarca é uma das rotas que o levará à montanha de sete cores, você partirá de Cusco, pegando
                        transporte público de Huayruropata (próximo ao Coliseu Fechado de Cusco), o custo do ingresso é
                        barato, em direção a Checacupe, você passe a rota por Andahuayllillas, Quiquijana e finalmente
                        chegará a Cusipata.<br> Deve-se notar que esta rota é bastante exigente, para a qual é
                        necessário que você esteja preparado fisicamente.<br>
                        Os quadriciclos são uma boa opção para algumas pessoas que optam por chegar à Montanha das Sete
                        Cores, pela rota de Pitumaca. O aluguel dos ATVs é de aproximadamente 100 dólares, o aluguel é
                        opcional e dependerá da empresa que os aluga.<br><br>
                    </p>

                    <h3>Rota Cusipata:</h3>
                    <p class="p-tours">
                        Esta rota é recente e menos exigente para os visitantes da Montanha das Sete Cores, pois na rota
                        de Pitumarca, você partirá de Cusco, em direção a Cusipata até chegar a Phuluwasipata, onde
                        iniciará a caminhada, lembre-se que subirá aproximadamente 100 quilômetros da cidade de Cusco, e
                        você subirá o cume de altitude localizado a 5.200 metros acima do nível do mar (m.s.n.m). A rota
                        Cusipata tem uma opção para visitantes que sentem que não podem continuar com a caminhada.
                        <br><br>

                        Estas são as duas maneiras diferentes de chegar a Vinuncunca, a maravilhosa montanha de sete
                        cores, você escolhe, de acordo com seus custos, no entanto recomendamos que você tenha o serviço
                        de uma agência, pois isso garantirá uma nova aventura, a preços acessíveis . mais confortável e
                        você não precisará se preocupar, apenas aproveite sua caminhada.<br>
                        Lembre-se que você pode procurar a agência que melhor lhe convier, mas não esqueça que esta
                        agência deve ter a respectiva Licença de Funcionamento e também possuir um Certificado emitido
                        pela Diretoria Regional de Comércio Exterior e Turismo de Cusco (DIRCETUR), para garantir sua
                        segurança. .
                    </p>
                    </p> <br><br><br>
                    <h5 class="h5-tours text-center"> <u>Experiência de caminhada até Vinicunca
                        </u></h5>
                    <p class="p-tours">
                        O trekking pela Montanha das 7 Cores é de grande dificuldade e bastante penoso, pois é feito em
                        altitude elevada, atingindo aproximadamente 5.200 metros. <br>Mas, se você precisar de ajuda,
                        você tem
                        várias opções: <br><br>
                        • Você pode andar a cavalo quase até o topo da Rainbow Mountain por um custo adicional. <br>
                        • Durante o trajeto até a Montanha das 7 Cores há pessoas vendendo bebidas, frutas, etc, não é
                        necessário carregar uma mochila muito ornamentada.<br>
                        • Há também banheiros ao longo do caminho, para qualquer emergência. <br>
                        Basta caminhar por vales incríveis e montanhas coloridas que se encontram ao longo dos 5 km até
                        chegar à Montanha Arco-Íris, a caminhada já vale a pena.<br>
                        Não só a Montanha das 7 Cores é um espetáculo, mas tanto ou mais, todo o caminho para
                        alcançá-la.<br>
                        E as pessoas que conduzem os cavalos, com trajes tradicionais feitos de alpaca e com cores
                        intensas, também fazem parte desse quadro que parece um documentário. Ótimo onde quer que você
                        olhe...<br> <br>

