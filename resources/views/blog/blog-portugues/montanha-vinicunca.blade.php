                    <h2><u>Localização da montanha colorida</u></h2>
                    <p class="p-tours">
                        A montanha está localizada na Cordilheira del Vilcanota a 5.200 metros acima do nível do mar,
                        entre os distritos de Cusipata, província de Quispicanchi, e Pitumarca, província de
                        Canchis.<br>
                        Suas encostas e cumes são tingidos por faixas de tons intensos de fúcsia, turquesa, lavanda e
                        ouro.
                        O espetáculo visual oferecido pela Vinicunca atrai visitantes desde o início de 2016, segundo
                        relatos da Prefeitura de Pitumarca. <br>
                        Em pouco mais de dois anos, os viajantes que vêm tirar fotos com o cume passaram de algumas
                        dezenas para cerca de 1.000 por dia, segundo a mídia peruana, apesar do frio e da altitude do
                        local.<br>
                        Graças à sua grande popularidade, também impulsionada em grande parte pelas várias redes
                        sociais, a montanha tornou-se um destino regular nos rankings de atrações turísticas
                        mundiais.<br>
                        Por exemplo, em agosto de 2017, ele apareceu na lista do site "Business Insider" de 100 lugares
                        para visitar antes de morrer recomendados por especialistas em viagens.
                        O impacto turístico de Vinicunca é bastante recente, mas a história da serra e de suas cores
                        começou há milhões de anos.

                    </p> <br>

                    <img src="{{ asset('img/blog/Vinicunca-portada.webp') }}" alt="Vinicunca" class="img-fluid"
