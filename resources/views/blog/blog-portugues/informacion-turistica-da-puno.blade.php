                    <h2 class="h5-tours">História de Puno</h2>
                    <p>
                        A região de Puno foi território das culturas Pucará e Tiahuanaco. Hoje existem inúmeros
                        vestígios que demonstram a ocupação precoce de Puno por essas e outras culturas que sucederam a
                        grande cultura inca. Ao longo dos anos, Puno tornou-se uma cidade próspera às margens do Lago
                        Titicaca. E sua população era composta por Qollas, Aymaras e Quechuas.<br><br>
                        Anos depois, segundo algumas lendas e histórias mitológicas, alguns dos antigos habitantes de
                        Puno migraram para o oeste e deram origem a uma das maiores culturas da América do Sul: a
                        cultura inca.<br><br>
                        Durante a colônia, a cidade de Puno caracterizou-se como uma vila modesta. Mas em 4 de novembro
                        de 1668, o vice-rei conde de Lemos fez dela a capital da província de Paucarcolla. A partir de
                        então, deixou de se chamar San Juan Bautista de Puno e passou a se chamar San Carlos de
                        Puno.<br><br>
                        No entanto, a região de Puno foi criada como região apenas em 26 de abril de 1822.
                    </p>

