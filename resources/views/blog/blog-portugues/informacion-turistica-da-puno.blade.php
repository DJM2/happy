                    <h2 class="h5-tours">O lago Titicaca.</h2>
                    <p class="p-tours">
                        O Lago Titicaca é o lago de água doce mais alto e navegável do mundo. Este Patrimônio Natural
                        está localizado na região de Puno e é compartilhado com o país vizinho da Bolívia. Dentro do
                        Lago Titicaca existem ilhas majestosas que são habitadas por pescadores e agricultores. As ilhas
                        mais importantes, onde os habitantes locais ainda praticam seus costumes ancestrais, são: as
                        ilhas flutuantes dos Uros, a Ilha de Taquile e Amantaní.<br><br>
                        Ao redor do místico e sagrado Lago Titicaca estão assentadas antigas cidades. Os habitantes
                        dessas cidades se sustentam principalmente com a renda que recebem do turismo.
                        Atualmente, o turismo no Lago Titicaca está aumentando e os muitos grupos de turistas que
                        visitam essas ilhas vêm de todo o mundo em busca de conhecer novas culturas. Todos esses
                        viajantes intrépidos navegam diariamente pelas frias águas azuis do Lago Titicaca para chegar a
                        essas belas ilhas flutuantes.<br><br>
                        Segundo a mitologia andina, das águas cristalinas do Lago Titicaca surgiram os fundadores do
                        império inca, Manco Capac e Mama Occllo.
                    </p>
