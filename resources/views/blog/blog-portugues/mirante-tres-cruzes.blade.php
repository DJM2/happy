                    <h2>Como conseguir?</h2>
                    <p>
                        Da cidade de Cusco, pegamos nosso transporte em direção ao sudeste pela rodovia
                        pavimentada e percorremos 108 km em média 2 horas até chegarmos a PAUCARTAMBO, onde
                        observaremos a ponte de Carlos III, o templo, e dentro dele, a Virgen del Carmen, que
                        É muito venerado com música e danças em sua festa que começa em 15 de julho até 18 de dezembro.
                        Julho. <br><br>
                        De Paucartambo, continuaremos nossa viagem de 45 km por mais duas horas, em direção ao
                        mirante de três cruzes douradas que está localizado dentro do Parque Nacional do Manu, que é um
                        dos
                        as reservas que abrigam uma rica biodiversidade reconhecida mundialmente, atualmente
                        protegido do impacto humano e foi nomeado como patrimônio natural mundial em 1987.
                    </p>

                    <h3> <strong>Observação:</strong></h3>
                    <p>
                        Antes de ir ao mirante, você deve pagar um ingresso para o Parque Nacional do Manu de 10.00
                        nuevos soles para adultos e 6,00 soles para crianças.<br><br>
                        Após a sua experiência no miradouro, pode visitar, por exemplo, a zona histórica cultural
                        "Pusharo" com um pagamento de renda extra de 50,00 soles, bem como a área do Rio Manu com um
                        pagamento de entrada de 150,00 soles e você terá que ser acompanhado por um guarda florestal
                        autorizado.
                    </p>
                    <h2>O que você deve trazer?</h2>
                    <ul>
                        <li>Roupas quentes, leves e impermeáveis.</li>
                        <li> Mochila.</li>
                        <li> Protetor solar e repelente.</li>
                        <li> Boné ou chapéu.</li>
                        <li> Água e lanches.</li>
                        <li> Dinheiro.</li>
                        <li>Câmera.</li>
                    </ul>

                    <h2>Qual é a melhor época para ir?</h2>
                    <p>
                        Recomenda-se a visita ao miradouro entre os meses de Maio a Julho; com destaque para os dias 20,
                        21 e 22 de Junho já que nestes dias ocorre o solstício de inverno e esperamos que não amanheça
                        com nuvens que impedem que você tenha vistas impressionantes; ou ainda outra data imperdível é
                        na festa de a Virgen del Carmen que começa em 15 de julho e termina em 18 de julho, onde
                        milhares de devotos conhecer e apresentar atividades artísticas, religiosas, culturais e
                        gastronômicas assim seus costumes e tradições a todos os moradores e visitantes, não à toa
                        Paucartambo é reconhecida como "capital popular de Cusco".
                    </p>
                    <h2> Você se atreve a ir ver o nascer do sol?</h2>

                    <p>Esta experiência é altamente recomendada para quem gosta de natureza e quer sair do
                        estresse da cidade seja na companhia de amigos e/ou familiares. <br><br>
                        O horário para contemplar esse fenômeno natural é das 4h30 às 5h30 da manhã.
                        Os raios do sol iluminam como caminhos no céu úmido por vários minutos, distorcendo o
                        imagem dando uma ilusão de ótica de três sóis, como se cada sol saltasse de um lado para o
                        outro.<br><br>
                        Algumas agências oferecem um passeio de dois dias para Paucartambo. Começando muito cedo na
                        cidade de Cusco em direção a Paucartambo, e durante a viagem faça algumas paradas para visitar
                        o complexo arqueológico de "Ninamarca" e suas chullpas pré-incas. <br> <br>
                        Ao chegar à cidade
                        Visita o templo da Virgen del Carmen ou o museu de danças típicas. A caminhada até o mirante
                        das três travessias é feita à noite e acampando em locais autorizados e aguardando
                        Espetacular nascer do sol que ilumina a densa selva de Manu.
                    </p>

