                        Paucartambo es un distrito de Cusco localizado a 2900 m.s.n.m, com uma variedade de flora, fauna
                        e costumes ancestrais. <br>
                        En el mirador de tres cruces con su bosque nuboso a 3850m.sn.n.m es possivel observar un
                        espectáculo natural, contemplando cores e tonalidades em el cielo, la luz y como va saliendo
                        o sol de uma forma muito diferente do que podemos estar acostumados, este fenômeno se observa
                        solo em 2 lugares no mundo: Paucartambo y Japón, por consiguiente esta experiencia siempre la
                        llevaras en tu mente como algo surreal.<br><br>
                        A la vez de este mirador se puede observar a cadeia de montanhas do nevado Ausangate que
