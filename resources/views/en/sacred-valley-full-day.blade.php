                        Valley, named for being the main supplier of food for the Inca empire, sits on the banks of the
                        Urubamba River. The Incas built archaeological centers, along its banks, that replicated the
                        constellations that they knew of the Andean sky, so this contributes even more to the beauty of
                        this charming valley. On the way back to Cusco we will make a stop at CHINCHERO, with its 17th
                        century colonial temple, its agricultural sector dating from the time of the Incas and its
                        population dedicated to textile art located in the highest part of Cusco over 3700 meters above
                        sea level. It will lead to an exciting and unforgettable experience of our tour.
                    </p><br>
                    <h5 class="h5-tours"> Itinerary:</h5>
                  
                    <p class="p-tours">
                        We start with our tour to the Sacred Valley of the Incas early around 08:00 a.m. With the pick
                        up from the points allowed in the historic center of the city, we go to the archaeological site
                        of PISAQ, located 32 km (20 miles) from the city of Cusco, 2,972 above sea level, place that has
                        the best terraces system achieved by the Incas in all the Andes, stone architecture
                        constructions and important temples of the Inca culture. We will be able to visit the "INDIAN
                        MARKET" the handicraft fair, among which beautiful textile crafts and ceramic crafts with
                        reproductions of Inca archaeological pieces stand out. Then we will go to the town of Urubamba,
                        which is within the Sacred Valley circuit, we will taste a delicious buffet lunch with a varied
                        gastronomy of local and international dishes.<br><br>

                        We will continue our tour and we will go to the town of <fuerte>OLLANTAYTAMBO</fuerte>, it is
                        located 97 km (60
                        miles) from Cusco, 2,846 above sea level. This town has the complexity and beauty of the Inca
                        constructions and the contemporary town. At the top of the archaeological complex, constructions
                        of religious and astronomical importance stand out, as well as a panoramic view of Ollantaytambo
                        called LIVING INCA PEOPLE.<br><br>

                        We go to the town of<fuerte> Chinchero</fuerte>, located 28 km at a high altitude of 3,750
                        meters above sea
                        level, famous for its fabrics and the most typical population of the Sacred Valley of the Incas,
                        it is a distinctly Inca city. In the Archaeological Ensemble of Chinchero, its platforms attract
                        attention, which allows us to understand that it was a center of agricultural production in Inca
                        times. A warehouse was also built and the entire complex was provided with a very efficient
                        irrigation system.<br><br>

                        Finally, we will visit the famous Chinchero market, where the native inhabitants, dressed in
                        colorful typical costumes, gather in the main square to exchange their products. Finally, we
                        will return to the city of Cusco arriving around 7:00 p.m. leaving them near the main square of
                        Cusco.
