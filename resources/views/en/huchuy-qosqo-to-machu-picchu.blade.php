                    <h5 class="h5-tours">Overview:</h5>
                    <p class="p-tours">
                        HUCHUY QOSQO, two Quechua terms that mean "SMALL CUSCO"; It is a fortress built in the upper
                        part of the LAMAY district in the SACRED VALLEY OF THE INCAS, it was WIRACOCHA one of the most
                        important rulers of the Inca period who ordered to build this construction around the 1420s.
                        Today one of the places with greater tourist influx due to the connection that Cusco has with
                        the Sacred Valley of the Incas, using part of the sacred path of our ancestors the great QAPAC
                        ÑAN, this route takes us to know the domain that our Quechua people had over the mountains in
                        time Of the Incas. With impressive views of the most imposing snow-capped mountains of the
                        Sacred Valley El Chicón, El Puma huanca and El Verónica or wilka weqe.
                    </p><br>
