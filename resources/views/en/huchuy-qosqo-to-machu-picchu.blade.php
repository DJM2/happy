                    <p class="p-tours">
                        Our adventure begins with the pick up from the hotel at 5:00 a.m. and then we go to the town of
                        TAUCA (3900m.s.n.m) in the district of Chinchero (3750m.s.n.m). At this point we will leave the
                        bus to begin our walk, by the height at which we are we will be able to appreciate the
                        cultivation of potatoes, mullets, olluco and some Andean cereals; After 3 hours of ascent we
                        will arrive at the Paso de HUCHUY QOSQO (4220m.s.n.m), called Puna, this place covered with
                        straw and typical flora of the region. We can also enjoy lagoons formed naturally by rainwater;
                        We begin our descent observing the change of vegetation on the way until we arrive at the
                        Archaeological site of HUCHUY QOSQO, where we will have enough time to enjoy our box lunch or
                        lunch, take pictures and explore the place that has important enclosures, ceremonial centers and
                        sectors clearly agricultural. Continuing with the tour we will make a descent towards LAMAY
                        located in the Sacred Valley of the Incas, meeting point with the tourist transport that will
                        take us to the town of Ollantaytambo known as "Living Inca town", after a dinner in a restaurant
                        we will finally board the train that will take us to Machupicchu and spend the night in a
                        comfortable hostel.
