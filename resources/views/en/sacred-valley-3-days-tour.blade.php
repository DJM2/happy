                        named for being the main supplier of food for the Inca empire, sits on the banks of the Urubamba
                        River. The Incas built archaeological centers, along its banks, that replicated the
                        constellations that they knew of the Andean sky, so this contributes even more to the beauty of
                        this charming valley. Connecting immediately with the Inca trail that begins at km 104 where we
                        will enjoy a unique nature connected with culture when we walk along the great Inca road that
                        leads to Machupicchu and the great opportunity to visit the citadel in two shifts in the
                        afternoon and at the morning.
