                    <h5 class="h5-tours"> Itinerario:</h5>
                   
                    <p class="p-tours">
                        Saliendo de la ciudad del Cusco a las 4:30 am nos dirigimos hacia el sur, llegando al distrito
                        de Cusipata donde tendremos el desayuno; continuaremos el viaje por 1 hora y media
                        aproximadamente hasta llegar al punto de inicio de nuestra caminata y final de buses (4600
                        m.s.n.m.), Empezaremos la caminata cuesta arriba por un aproximado de 2 horas durante el camino
                        deleitamos nuestra vista con impresionantes montañas empinadas, pampas cubiertas de ichu o paja,
                        vegetación propia de la puna Peruana; también podremos apreciar fauna colorida de aves en su
                        hábitat. Arribaremos a la cumbre de la montaña y tomaremos la montaña hacia la izquierda para
