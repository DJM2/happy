                        una altura de 5200 m.s.n.m. En la mitad de la década del 2010 empezó su masificación turística
                        atraídos por la serie de franjas de varios colores, esto debido a su composición mineralógica
                        presente en las laderas y cumbres. Hoy en día considerada también como la JOYA escondida de los
