                        regreso tomaremos la ruta hacia el VALLE ROJO, luego de disfrutar este hermoso paisaje
                        retornaremos hacia nuestro lugar de inicio o estación de buses para continuar el retorno,
                        haciendo una parada en el mismo lugar de desayuno y allí tener el almuerzo respectivo. Estaremos
                        en la ciudad del Cusco entre las 17:00hrs y 17:30 hrs.
