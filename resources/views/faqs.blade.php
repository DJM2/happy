                                    <p>
                                        Altitude sickness or soroche, as it is also known in the Andes, is a
                                        disease caused by lack of oxygen in the body. as we climb
                                        higher, the atmospheric pressure is lower, so we feel that "we lack the
                                        air".<br><br>
                                        It occurs when ascending abruptly to altitudes above 2,400
                                        meters above sea level, like when you travel from Lima (161 m.a.s.l.) to Cusco
                                        (3,399 m.s.n.m) by plane.
                                    </p>
                                    <h5>Symptoms of altitude sickness</h5>
                                    <p>
                                        Altitude sickness is very easy to recognize by its symptoms. like less oxygen
                                        reaches the body, it feels:
                                    </p>
                                    <ul>
                                        <li>Headache: lack of oxygen increases capillary pressure and
                                            swelling of the head, causing pain. This symptom occurs in 80% of the cases
                                            of people who climb from soon above 3,000 m altitude.
                                        </li>
                                        <li>Dizziness and nausea: It is normal to feel a little pressure in the head
                                            that causes dizziness and mild nausea.</li>
                                        <li>General tiredness: the lack of oxygen causes a feeling of fatigue that
                                            It should be attended by resting and slowing down the movements. The
                                            The recommendation is to walk slowly, not to run or shake.</li>
                                        <li>Swelling of hands and feet: it is one of the most common symptoms and the
                                            first to disappear once the body acclimatizes to the altitude.</li>
                                    </ul>
                                    <p>
                                        It is believed that altitude sickness does not affect people who are fit, but
                                        this is a myth. There are other factors like age, moving up too fast, doing
                                        physical activity without acclimatization, poor sleep, overeating, or hydration
                                        little that also take their toll.
                                    </p>
                                    <h5>Some tips to prevent altitude sickness are:</h5>
                                    <ul>
                                        <li>Travel by bus to places of high altitude such as Arequipa, Cusco or Puno.
                                            The Progressive ascent decreases the chances of suffering from altitude
                                            sickness.
                                        </li>
                                        <li>Take a special pill against soroche or mountain sickness. Peru there is
                                            options like Sorojchipills that can be taken to prevent
                                            symptoms.</li>
                                        <li>Chewing coca leaves or drinking them in tea: it is important not to swallow
                                            the coca leaves! Just chew and swallow the saliva. You can also drink mate
                                            of coca.</li>
                                        <li>Maintain a balanced diet, low in salt and very light so that the body
                                            can process height better.</li>
                                        <li>Drink plenty of water before and during the trip so that the body is more
                                            ready.</li>
                                    </ul>
