                        Nuestra aventura inicia con el recojo del hotel a las 5:00am para luego dirigirnos hacia el
                        poblado de TAUCA (3900m.s.n.m) en el distrito de Chinchero (3750m.s.n.m). En este punto
                        dejaremos el bus para comenzar nuestra caminata, por la altura a la que nos encontramos podremos
                        apreciar el cultivo de papa, lisas, olluco y algunos cereales andinos; luego de 3 horas de
                        ascenso llegaremos al Paso de HUCHUY QOSQO (4220 m.s.n.m), denominado Puna, este lugar cubierto
                        de paja y flora típica de la región. También podremos disfrutar de lagunas formadas naturalmente
                        por aguas pluviales; empezamos nuestro descenso observando en el camino el cambio de vegetación
                        hasta arribar al sitio Arqueológico de HUCHUY QOSQO, donde tendremos el tiempo suficiente para
                        disfrutar nuestro box lunch o almuerzo, tomar fotografías y explorar el lugar que cuenta con
                        recintos importantes, centros ceremoniales y sectores netamente agrícolas. Continuando con el
                        recorrido haremos un descenso hacia LAMAY ubicado en el Valle Sagrado de los Incas punto de
                        encuentro con el transporte turístico que nos llevará hacia el pueblo de Ollantaytambo conocido
                        como “Pueblo inca viviente”, después de una cena en un restaurante finalmente abordaremos el
                        tren que nos conducirá hacia Machupicchu y poder pernoctar en un hostal confortable.
