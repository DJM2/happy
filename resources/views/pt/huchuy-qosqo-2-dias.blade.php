                    <h5 class="h5-tours">Vista General:</h5>
                    <p class="p-tours">
                        HUCHUY QOSQO, dos términos quechuas que significan “CUSCO PEQUEÑO”; es una fortaleza construida
                        en la parte alta del distrito de LAMAY en el VALLE SAGRADO DE LOS INCAS, fue WIRACOCHA uno de
                        los gobernantes más importantes de la época inca quien manda a edificar dicha construcción hacia
                        los años 1420. Hoy en día uno de los lugares con mayor afluencia turística por la conexión que
                        tiene el Cusco con el Valle Sagrado de los Incas utilizando parte del camino sagrado de nuestros
                        ancestros el gran QHAPAC ÑAN, esta ruta nos lleva a conocer el dominio que tuvieron sobre las
                        montañas nuestra gente quechua en el tiempo de los incas. Con vistas impresionantes de los
                        nevados más imponentes del Valle Sagrado El Chicón, El Puma huanca y el Verónica o wilka weqe.
