                        <p class="p-tours">
                            HUCHUY QOSQO, dois termos Quechua que significam "PEQUENO CUSCO"; É uma fortaleza construída
                            na parte alta do distrito de LAMAY no VALE SAGRADO DOS INCAS, foi WIRACOCHA um dos
                            governantes mais importantes do período Inca que mandou construir essa construção por volta
                            de 1420. Hoje um dos lugares Com maior afluxo turístico devido à conexão que Cusco tem com o
                            Vale Sagrado dos Incas, utilizando parte do caminho sagrado de nossos ancestrais o grande
                            QHAPAC ÑAN, este percurso nos leva a conhecer o domínio que nosso povo Quechua teve sobre as
                            montanhas no tempo Dos Incas. Com vistas impressionantes das montanhas nevadas mais
                            imponentes do Vale Sagrado: o Chicón, o Pumahuanca e a Verónica ou willka weqe.
