                        <h5 class="h5-tours">Roteiro:</h5>
                        <p class="p-tours">
                            Nossa aventura começa com o traslado do seu hotel às 5h00 da manhã para seguir em direção à
                            cidade de Tauca (3900m) no distrito de Chinchero (3750m). Neste ponto sairemos do ônibus
                            para iniciar nossa caminhada, na altura em que nos encontramos poderemos apreciar o cultivo
                            de batata, salmonete, batata doce e alguns cereais andinos; Após de 3 horas de subida
                            chegaremos ao ponto mais alto de HUCHUY QOSQO (4220 m), denominado Puna, este local esta
                            coberto de palha e flora típica da região. Também podemos desfrutar de lagoas formadas
                            naturalmente pela água da chuva; Iniciamos nossa descida observando a mudança da vegetação
                            no caminho até chegarmos ao sítio Arqueológico de HUCHUY QOSQO, onde teremos tempo
                            suficiente para desfrutar de nosso lanche ou almoço, tirar fotos e explorar o local que
                            possui importantes recintos, centros cerimoniais e setores claramente agrícolas. Continuando
                            com o passeio faremos uma descida ao LAMAY localizado no Vale Sagrado dos Incas, ponto de
                            encontro com o ônibus para retornar à cidade de Cusco, chegando aproximadamente às 18:00 no
                            hotel.
                        </p>
