                    <h5 class="h5-tours">Visão general:</h5>
                    <p class="p-tours">
                        O Tour ao Vale Sagrado dos Incas que dura o dia todo levará você aos lugares mais clássicos;
                        Sítio arqueológico PISAQ, MERCADO TRADICIONAL, OLLANTAYTAMBO e CHINCHERO. O Vale Sagrado,
                        batizado por ser o principal fornecedor de alimentos para o império inca, fica às margens do rio
                        Urubamba. Os incas construíram centros arqueológicos, ao longo de suas margens, que reproduziam
                        as constelações que eles conheciam do céu andino, o que contribui ainda mais para a beleza deste
                        encantador vale. No caminho de volta a Cusco faremos uma parada em CHINCHERO, com seu Templo
                        Colonial do século XVII, seu setor agrícola que data dos tempos dos Incas, e sua população
                        dedicada à arte têxtil localizada na parte mais alta de Cusco a mais de 3700 metros acima do
                        mar. Isso nos levará a uma experiência emocionante e inesquecível de nosso passeio.


                    </p><br>
                    <h5 class="h5-tours"> ROTEIRO:</h5>

                    <p class="p-tours">
                        Começamos nossa excursão ao Vale Sagrado dos Incas cedo, por volta das 08:00 horas. Com a pegada
                        desde os pontos permitidos no centro histórico da cidade, nos dirigimos ao Sítio Arqueológico de
                        PISAQ, localizado a 32 km (20 milhas) da cidade de Cusco, a 2.972 m.a.s.l. Local que possui o
                        melhor sistema de andenería alcançado pelos Incas em todos os Andes, construções arquitetônicas
                        de pedra e importantes templos da cultura Inca. Poderemos visitar o "MERCADO INDIANO" a feira de
                        artesanato, entre a qual se destacam belos artesanatos têxteis e cerâmicos com reproduções de
                        peças arqueológicas incas. Em seguida iremos para a cidade de Urubamba, que está localizada
                        dentro do circuito Vale Sagrado, degustaremos um gostoso almoço buffet com uma gastronomia
                        variada de pratos locais e internacionais.<br><br>

                        Continuaremos nosso tour em direção à cidade de OLLANTAYTAMBO, localizada a 97 km (60 milhas) de
                        Cusco, a 2.846 metros acima do nível do mar. Esta cidade tem a complexidade e a beleza das
                        construções incas e da cidade contemporânea. No topo do complexo arqueológico destacam-se
                        edifícios de importância religiosa e astronômica, além de uma vista panorâmica de Ollantaytambo
                        denominada Povo Inca Viviente.<br><br>

                        Seguimos para o povoado de Chincheros, localizado a 28 km a uma altitude de 3.750 metros acima
                        do nível do mar, famoso por seus tecidos e a população mais típica do Vale Sagrado dos Incas, é
                        uma cidade distintamente inca. No Conjunto Arqueológico de Chinchero, as suas plataformas chamam
                        a atenção, o que nos permite compreender que foi um centro de produção agrícola na época dos
                        incas. Foi também construído um armazém e todo o complexo foi equipado com um sistema de
                        irrigação muito eficiente.<br><br>

                        Finalmente, visitaremos o famoso mercado de Chinchero, onde os habitantes nativos, vestidos com
                        trajes típicos coloridos, se reúnem na praça principal para trocar seus produtos. Finalmente,
                        retornaremos à cidade de Cusco chegando por volta das 19h00. deixando-os perto da Plaza de Armas
                        de Cusco.
                    </p>
